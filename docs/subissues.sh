Title: 103-A: [arb4j] Implement bytecode generation for ∞ convergence loops

Summary
-------
Design and implement the compiler bytecode emission so that NAry operations
(SumNode/ProductNode) detect an infinite upper bound (`upperLimit.isPositiveInfinity()`)
and emit a convergence-driven loop instead of a fixed-bound index loop. This is
the foundational change enabling automatic, data-driven truncation (Σ{..∞}).

Why
---
Current behavior emits bytecode that executes a fixed `for k = a..N` loop.
N is currently supplied as a context variable and mutated externally. That
approach is incorrect for convergent series: the correct termination is data-driven
(convergence), not a prior allocated integer bound.

Goals
-----
- Detect ∞ upper bound in NAry operation nodes
- Emit inline convergence-based loop in the generated bytecode
- Provide deterministic divergence detection (running-max / peakCount)
- Unit tests for dispatch & basic convergence/divergence behaviors

Design / Implementation Plan
----------------------------
Phase 1 — Design the canonical loop (pseudocode):
  tolerance = 2^{-bits}
  runningMax = 0
  peakCount = 0
  index = lowerLimit
  accumulator = identity

  loop:
    term = operand.evaluate(index, 1, bits, scratch)
    if term is null: throw ArithmeticException("operand returned null")
    termAbs = |term|
    if termAbs < tolerance:
      accumulator = accumulator ∘ term
      break
    if termAbs > runningMax:
      runningMax = termAbs
      peakCount++
      if peakCount >= 3:
        throw ArithmeticException("series diverges: 3 consecutive new peaks")
    accumulator = accumulator ∘ term
    index++
    goto loop

Phase 2 — Locate generator entrypoints:
- File(s):
  - arb/expressions/nodes/nary/NAryOperationNode.java
  - arb/expressions/nodes/nary/SumNode.java
  - arb/expressions/nodes/nary/ProductNode.java
- Find the methods generating bounded loops and identify where the upperLimit comparison is emitted.

Phase 3 — Add ∞ detection & dispatch:
- Add utility: `boolean upperIsInfinity(Node upperLimit)` or reuse existing API if present.
- Emit conditional in code generation: if upperIsInfinity -> call convergence loop emitter; else -> emit existing bounded loop.

Phase 4 — Convergence bytecode emission:
- Emit local variables for:
  - tolerance (computed from bits): Real tolerance = 2^{-bits}
  - runningMax (Real)
  - peakCount (int)
  - index (Integer / primitive int, whichever is appropriate for index variable)
  - accumulator (type matches R)
- Emit operand evaluation at each iteration:
  - Must set operand's index variable into the operand instance prior to calling operand's evaluate.
  - Use same operand scratch variable strategy the compiler currently uses.
- Emit comparisons: absolute value, compare to tolerance; update runningMax and peakCount; throw ArithmeticException when peakCount >= 3 with diagnostic message including index, runningMax, last term magnitude and bits.
- Emit accumulation via existing fold operation (add/mul).

Diagnostics & Error Messages
----------------------------
When throwing ArithmeticException for divergence include:
- index
- peakCount
- runningMax
- last term magnitude
- tolerance and bits

This helps debugging mis-parameters (e.g. unsuitable model parameters) vs. code bugs.

Testing (unit and integration)
------------------------------
- Unit tests to verify dispatch:
  - SumNode chooses convergence path for ∞ and bounded path for finite upper limit.
- Convergence case tests:
  - Series Σ (1/2)^k converges to 2. Verify generator terminates early and returns ~2 within tolerance.
  - Series Σ 1/k^2 converges. Validate correctness.
  - Verify tolerance calculation scales with bits.
- Divergence tests:
  - Series with strictly increasing terms (e.g., k) should throw ArithmeticException after ~3 peaks.
  - Alternating series that converges (conditionally) must NOT be flagged as divergent incorrectly.
- Integration test:
  - Compile a small expression using Σ{..∞} and execute. Verify correct numeric output and no resource leak.

Files to Change
---------------
- arb/expressions/nodes/nary/NAryOperationNode.java — add the converge-path emission and dispatch.
- arb/expressions/nodes/nary/SumNode.java — ensure parse/AST accepts ∞ as upper bound (if not, add recognition).
- Tests: Add SumNodeConvergenceDispatchTest, NAryOperationNodeConvergentAccumulationTest

Exit Criteria
-------------
- Compiler emits the convergence loop when upper limit is ∞.
- Convergent numeric tests pass.
- Divergent numeric tests throw clearly diagnostic exceptions.
- No regression in finite-bound sum behavior.
BODY

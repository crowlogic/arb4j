set -euo pipefail

REPO="crowlogic/bonanzai"
PARENT_ISSUE=103

# Sanity checks ---------------------------------------------------------------
command -v gh >/dev/null 2>&1 || { echo "ERROR: gh (GitHub CLI) not found in PATH."; exit 1; }
if ! gh repo view "$REPO" >/dev/null 2>&1; then
  echo "ERROR: gh cannot access repository $REPO. Ensure you are authenticated and have permissions."
  exit 1
fi

# Temporary files cleanup
TMP_FILES=()
cleanup() {
  for f in "${TMP_FILES[@]:-}"; do
    [[ -f "$f" ]] && rm -f "$f"
  done
}
trap cleanup EXIT

# Helper: create an issue from a here-doc body
create_issue_from_heredoc() {
  local key="$1"
  local title="$2"
  local tmp
  tmp="$(mktemp "/tmp/${key}.XXXXXX.md")"
  TMP_FILES+=("$tmp")
  # Body provided via stdin (heredoc called by caller)
  cat > "$tmp"
  echo "Creating issue $key -> $title"
  # Use gh to create issue; return number
  num=$(gh issue create --repo "$REPO" --title "$title" --body-file "$tmp" --json number --jq '.number')
  echo "Created issue #$num for $key"
  echo "$num"
}


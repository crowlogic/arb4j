#
# Create detailed sub-issues for issue #103 in crowlogic/bonanzai using the
# GitHub CLI (gh). This script:
#  - creates issues 103-A .. 103-J with full, production-ready bodies
#  - posts a comment to the parent issue #103 listing the created issues
#
# Usage:
#   1) Install & authenticate gh: `gh auth login`
#   2) Save this script, chmod +x ./create-103-subissues.sh
#   3) Run: ./create-103-subissues.sh
#
# Preconditions:
#   - gh must be installed and authenticated as a user with permission to create
#     issues in the repository crowlogic/bonanzai.
#
# Safety:
#   - Script creates issues only. It does not modify repo files or branches.

  public static class CanonicalSfunc implements ComplexFunction, AutoCloseable, Initializable
  {
    public boolean        isInitialized;
    private Complex       lastV;
    private Complex       cachedResult;
    public final Integer  c1 = new Integer("1");
    public CanonicalA     a;
    public Integer        k;
    private final Complex tmpC1 = new Complex();
    private final Complex tmpC2 = new Complex();
    private final Integer tmpN1 = new Integer();
    private final Integer tmpN2 = new Integer();

    @Override
    public Class<Complex> domainType()
    {
      return Complex.class;
    }

    @Override
    public Class<Complex> coDomainType()
    {
      return Complex.class;
    }

    @Override
    public synchronized Complex evaluate(Complex v, int order, int bits, Complex result)
    {
      if (!isInitialized)
      {
        initialize();
      }
      if (order > 1)
      {
        throw new UnsupportedOperationException("derivatives not supported");
      }
      if (lastV != null && lastV.equals(v) && cachedResult != null)
      {
        return result.set(cachedResult);
      }
      if (lastV == null)
      {
        lastV = new Complex();
      }
      lastV.set(v);
      ComplexFunction a1 = a.evaluate(k.sub(c1, bits, tmpN1), order, bits, null);
      Complex r1 = a1.evaluate(v, order, bits, tmpC1);
      ComplexFunction a2 = a.evaluate(k.sub(c1, bits, tmpN2), order, bits, null);
      Complex r2 = a2.evaluate(v, order, bits, tmpC2);
      r1.mul(r2, bits, result);
      if (cachedResult == null)
      {
        cachedResult = new Complex();
      }
      cachedResult.set(result);
      return result;
    }

    @Override
    public void initialize()
    {
      if (isInitialized)
      {
        throw new AssertionError("Already initialized");
      }
      if (a == null)
      {
        throw new AssertionError("a is null");
      }
      isInitialized = true;
    }

    @Override
    public void close()
    {
      c1.close();
      tmpC1.close();
      tmpC2.close();
      tmpN1.close();
      tmpN2.close();
      if (lastV != null)
      {
        lastV.close();
      }
      if (cachedResult != null)
      {
        cachedResult.close();
      }
    }

    @Override
    public String typeset()
    {
      return toString();
    }

    @Override
    public String toString()
    {
      return String.format("Sfunc:v➔(a(%s-1)(v))*(a(%s-1)(v))", String.valueOf(k));
    }
  }

  public static void testHandWrittenNestedMutualRecursion()
  {
    try (CanonicalA a = new CanonicalA();
         CanonicalS shadowS = new CanonicalS();
         CanonicalS S = new CanonicalS();
         CanonicalA shadowA = new CanonicalA();
         Integer k = new Integer("2");
         Complex v = new Complex();
         Complex out = new Complex())
    {
      // Separate-instance wiring for the nested cluster: canonical a refers to
      // a distinct S instance, and canonical S refers to a distinct a instance.
      // Each shadow points back to its canonical partner.
      a.S = shadowS;
      shadowS.a = a;
      S.a = shadowA;
      shadowA.S = S;

      v.set(2, 0);

      ComplexFunction a2 = a.evaluate(k, 1, 128, null);
      a2.evaluate(v, 1, 128, out);
      assertEquals("4", out.toString());

      ComplexFunction S2 = S.evaluate(k, 1, 128, null);
      S2.evaluate(v, 1, 128, out);
      assertEquals("4", out.toString());
    }
  }


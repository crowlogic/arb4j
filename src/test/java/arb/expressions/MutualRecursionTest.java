  public static void testCompiledMutualRecursion()
  {
    Context ctx = new Context();
    ctx.declare("g", Integer.class, Real.class, RealSequence.class);

    RealSequence f = RealSequence.express("f:n➔when(n=0,1,else,g(n-1)+1)", ctx);
    RealSequence g = RealSequence.express("g:n➔when(n=0,1,else,f(n-1)+2)", ctx);

    try (Real value = new Real())
    {
      assertEquals("1", f.evaluate(new Integer("0"), 0, 128, value).toString());
      assertEquals("1", g.evaluate(new Integer("0"), 0, 128, value).toString());
      assertEquals("2", f.evaluate(new Integer("1"), 0, 128, value).toString());
      assertEquals("3", g.evaluate(new Integer("1"), 0, 128, value).toString());
      assertEquals("4", f.evaluate(new Integer("2"), 0, 128, value).toString());
      assertEquals("4", g.evaluate(new Integer("2"), 0, 128, value).toString());
    }
  }

  public static void testHandWrittenMutualRecursion()
  {
    try (CanonicalF f = new CanonicalF();
         CanonicalG gShadow = new CanonicalG();
         CanonicalG g = new CanonicalG();
         CanonicalF fShadow = new CanonicalF();
         Real value = new Real())
    {
      // Separate-instance wiring: the canonical f refers to a distinct g
      // instance, and the canonical g refers to a distinct f instance.  Each
      // shadow points back to its canonical partner, so the recursion
      // alternates between two buffers instead of aliasing the same instance.
      f.g = gShadow;
      gShadow.f = f;
      g.f = fShadow;
      fShadow.g = g;

      assertEquals("1", f.evaluate(new Integer("0"), 0, 128, value).toString());
      assertEquals("1", g.evaluate(new Integer("0"), 0, 128, value).toString());
      assertEquals("2", f.evaluate(new Integer("1"), 0, 128, value).toString());
      assertEquals("3", g.evaluate(new Integer("1"), 0, 128, value).toString());
      assertEquals("4", f.evaluate(new Integer("2"), 0, 128, value).toString());
      assertEquals("4", g.evaluate(new Integer("2"), 0, 128, value).toString());
    }
  }

  /**
   * Nested-function mutual recursion equivalent to the a/S cluster that
   * previously exposed cycle-initialization defects. Both a and S are indexed
   * functions of k returning a function of v.
   */
  public static void testCompiledNestedMutualRecursion()
  {
    Context ctx = new Context();
    ctx.declare("a", Integer.class, ComplexFunction.class, ComplexFunctionSequence.class);
    Sequence.compile("S", ComplexFunction.class, "S:k➔v➔a(k-1)(v)·a(k-1)(v)", ComplexFunctionSequence.class, ctx);

    ComplexFunctionSequence a = ComplexFunctionSequence.express("a:k➔v➔when(k=1,v,else,S(k)(v))", ctx);

    try (Integer k = new Integer("2"); Complex v = new Complex(); Complex out = new Complex())
    {
      v.set(2, 0);
      ComplexFunction a2 = a.evaluate(k, 1, 128, null);
      a2.evaluate(v, 1, 128, out);
      assertEquals("4", out.toString());
    }
  }

  /**
   * Hand-written, separate-instance version of the nested a/S cluster.
   * Canonical a and S each point to a distinct shadow peer, breaking the
   * direct object cycle and giving each level its own scratch buffers while
   * still alternating back to the canonical instance for memoization.
   */
  public static class CanonicalA implements ComplexFunctionSequence, AutoCloseable, Initializable
  {
    public boolean                       isInitialized;
    private final IndexCache<ComplexFunction> cache = new IndexCache<>();
    public CanonicalS                    S;
    private final Integer                c1   = new Integer("1");

    @Override
    public Class<Integer> domainType()
    {
      return Integer.class;
    }

    @Override
    public Class<ComplexFunction> coDomainType()
    {
      return ComplexFunction.class;
    }

    @Override
    public synchronized ComplexFunction evaluate(Integer k, int order, int bits, ComplexFunction result)
    {
      if (!isInitialized)
      {
        initialize();
      }
      ComplexFunction cached = Function.peek(cache, k);
      if (cached != null)
      {
        return cached;
      }
      CanonicalAfunc func = new CanonicalAfunc();
      if (func.k == null)
      {
        func.k = new Integer();
      }
      func.k.set(k);
      func.S = this.S;
      func.initialize();
      Function.poke(cache, k, func);
      return func;
    }

    @Override
    public void initialize()
    {
      if (isInitialized)
      {
        throw new AssertionError("Already initialized");
      }
      if (S == null)
      {
        throw new AssertionError("S is null");
      }
      isInitialized = true;
    }

    @Override
    public void close()
    {
      c1.close();
    }

    @Override
    public String typeset()
    {
      return toString();
    }

    @Override
    public String toString()
    {
      return "a:k➔v➔when(k=1,v,else,S(k)(v))";
    }
  }

  public static class CanonicalAfunc implements ComplexFunction, AutoCloseable, Initializable
  {
    public boolean        isInitialized;
    private Complex       lastV;
    private Complex       cachedResult;
    public final Integer  c1 = new Integer("1");
    public CanonicalS     S;
    public Integer        k;

    @Override
    public Class<Complex> domainType()
    {
      return Complex.class;
    }

    @Override
    public Class<Complex> coDomainType()
    {
      return Complex.class;
    }

    @Override
    public synchronized Complex evaluate(Complex v, int order, int bits, Complex result)
    {
      if (!isInitialized)
      {
        initialize();
      }
      if (order > 1)
      {
        throw new UnsupportedOperationException("derivatives not supported");
      }
      if (lastV != null && lastV.equals(v) && cachedResult != null)
      {
        return result.set(cachedResult);
      }
      if (lastV == null)
      {
        lastV = new Complex();
      }
      lastV.set(v);
      switch (k.getSignedValue())
      {
      case 1:
        result.set(v);
        break;
      default:
        ComplexFunction sFunc = S.evaluate(k, order, bits, null);
        result = sFunc.evaluate(v, order, bits, result);
      }
      if (cachedResult == null)
      {
        cachedResult = new Complex();
      }
      cachedResult.set(result);
      return result;
    }

    @Override
    public void initialize()
    {
      if (isInitialized)
      {
        throw new AssertionError("Already initialized");
      }
      if (S == null)
      {
        throw new AssertionError("S is null");
      }
      isInitialized = true;
    }

    @Override
    public void close()
    {
      c1.close();
      if (lastV != null)
      {
        lastV.close();
      }
      if (cachedResult != null)
      {
        cachedResult.close();
      }
    }

    @Override
    public String typeset()
    {
      return toString();
    }

    @Override
    public String toString()
    {
      return String.format("afunc:v➔when(%s=1,v,else,S(%s)(v))", String.valueOf(k));
    }
  }


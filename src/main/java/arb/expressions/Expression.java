  /**
   * Emit {@code public void invalidateCache()} that:
   * <ol>
   * <li>returns immediately if the per-instance {@code invalidatingCache} flag is
   * already set, then sets it — terminating recursion in a cyclic reference
   * graph (the mutually-recursive Müntz {@code a ↔ S} cluster, or a self-
   * referential recurrence) the same way {@code close()} uses the {@code closed}
   * flag, with no runtime visited-list;</li>
   * <li>resets {@code this.staticPrecision = -1} when this expression has hoisted
   * static subexpressions, forcing the next {@code evaluate()} call to re-run
   * {@code evaluateStaticSubexpressions};</li>
   * <li>recursively calls {@code invalidateCache()} on every inlined nested
   * {@link Function} field (the {@link #referencedFunctions} entries) so their
   * hoisted v-dependent subtrees are also flushed, then clears the flag.</li>
   * </ol>
   * Emitted whenever {@link #hasStaticNodes} is true OR
   * {@link #referencedFunctions} is non-empty — otherwise the {@link Function}
   * interface's no-op default is inherited.
   */
  protected ClassVisitor generateInvalidateMethod(ClassVisitor classVisitor)
  {
    if (!hasStaticNodes && referencedFunctions.isEmpty() && !shouldCache() && !shouldCacheValueBacking())
    {
      return classVisitor;
    }
    generateInvalidateMethod(classVisitor, "invalidateCache", true);
    // Non-propagating local-cache clear: drops only THIS function's hoisted
    // state (index cache / value-backing / static precision) without cascading
    // to referenced producers. A parameter rebind uses it to refresh the caches
    // that depend on the changed variable while preserving the upstream caches
    // that do not (see MuntzPadeApproximant.rebind, which keeps the v-independent
    // σ-table while dropping the v-dependent αv/βv/hv/Pn/Φ caches).
    if (hasStaticNodes || shouldCache() || shouldCacheValueBacking())
    {
      generateInvalidateMethod(classVisitor, "invalidateLocalCache", false);
    }
    return classVisitor;
  }

  protected ClassVisitor generateInvalidateMethod(ClassVisitor classVisitor, String methodName, boolean propagate)
  {
    var     mv          = classVisitor.visitMethod(Opcodes.ACC_PUBLIC, methodName, "()V", null, null);
    mv.visitCode();
    boolean cached      = shouldCache();
    // Reentrancy flag keyed on the SHARED identity of the cluster:
    //
    //  - cache-bearing class: a self-referential sequence (the Müntz a/aoperand
    //    cluster) is evaluated through an N-deep chain of DISTINCT instances —
    //    one per recursion level for scratch isolation — that all SHARE one
    //    IndexCache (the parent stores child.cache = this.cache, see
    //    generateNestedFunctionInstanceCacheSharing below). A per-instance flag
    //    cannot stop a walk over distinct instances and overflows the stack at
    //    large order. The flag on the shared cache collapses the whole chain to a
    //    single visit: the first instance clears the one shared cache and sets
    //    cache.invalidating, every other chain member (reached via the self-edge
    //    or the operand bridge) sees it set and returns. This is the minimal,
    //    collection-free form of "invalidate each cluster once" the same way
    //    close() uses `closed`. Mutually-recursive sequences with independent
    //    caches each carry their own flag and are cleared exactly once.
    //
    //  - cacheless class: per-instance invalidatingCache over a finite ref graph.
    var notInvalidating = new Label();
    if (cached)
    {
      loadThisOntoStack(mv);
      mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "cache", Type.getDescriptor(IndexCache.class));
      mv.visitFieldInsn(Opcodes.GETFIELD, Type.getInternalName(IndexCache.class), "invalidating", "Z");
      mv.visitJumpInsn(Opcodes.IFEQ, notInvalidating);
      mv.visitInsn(Opcodes.RETURN);
      mv.visitLabel(notInvalidating);
      loadThisOntoStack(mv);
      mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "cache", Type.getDescriptor(IndexCache.class));
      mv.visitInsn(Opcodes.ICONST_1);
      mv.visitFieldInsn(Opcodes.PUTFIELD, Type.getInternalName(IndexCache.class), "invalidating", "Z");
    }
    else
    {
      loadThisOntoStack(mv);
      mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "invalidatingCache", "Z");
      mv.visitJumpInsn(Opcodes.IFEQ, notInvalidating);
      mv.visitInsn(Opcodes.RETURN);
      mv.visitLabel(notInvalidating);
      loadThisOntoStack(mv);
      mv.visitInsn(Opcodes.ICONST_1);
      mv.visitFieldInsn(Opcodes.PUTFIELD, internalName(), "invalidatingCache", "Z");
    }
    if (hasStaticNodes)
    {
      // this.staticPrecision = -1
      loadThisOntoStack(mv);
      mv.visitInsn(Opcodes.ICONST_M1);
      mv.visitFieldInsn(Opcodes.PUTFIELD, internalName(), "staticPrecision", "I");
    }
    if (cached)
    {
      loadThisOntoStack(mv);
      mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "cache", Type.getDescriptor(IndexCache.class));
      if (isGeneratedFunctional())
      {
        // Functional codomain: the cache entries are inner Function instances
        // this sequence constructed and owns (e.g. Φ caches Φfunc(M)). They are
        // reusable across a change to a Context variable (v) or to parameters —
        // only their own per-call memo / value sub-cache goes stale — so the
        // correct action is to invalidate each entry IN PLACE (it then closes
        // the native memory it owns) and KEEP the instance. Dropping them via
        // clear() orphaned their owned scratch — a leak — and forced their
        // reconstruction every build, the allocation churn that made a v-sweep
        // (and the calibration that drives one per LM step) slow and unbounded.
        mv.visitInsn(propagate ? Opcodes.ICONST_1 : Opcodes.ICONST_0);
        generateVirtualMethodInvocation(mv, IndexCache.class, "invalidateEntries", void.class, boolean.class);
      }
      else
      {
        // Value codomain: the per-index table is keyed only on the domain index
        // k, so any change to another Context variable (e.g. v in the Müntz a/S
        // recurrence) must drop every entry, otherwise a(k) returns the previous
        // v's value at the same k. clear() also closes the owned value copies,
        // freeing their native memory. One clear of the shared cache drops every
        // level of a self-referential chain at once.
        generateVirtualMethodInvocation(mv, IndexCache.class, "clear", void.class);
      }
    }
    if (shouldCacheValueBacking())
    {
      // Issue #1005: drop the per-instance value-backing cache. Setting lastV
      // to null guarantees the next evaluate() call goes to the body, since
      // every non-null user-supplied input v will fail the IF_ACMPNE check.
      //
      // lastV is an OWNED arb instance (e.g. new Complex()); arb types have no
      // finalizer, so nulling it without close() orphans its native memory —
      // every invalidate (a per-step recalibration drives many) leaks exactly
      // one. Close it (null-guarded, via the shared close-invocation helper)
      // before nulling when the domain type owns native memory.
      if (AutoCloseable.class.isAssignableFrom(domainType))
        generateCloseFieldCall(mv, "lastV", domainType);
      loadThisOntoStack(mv);
      mv.visitInsn(Opcodes.ACONST_NULL);
      mv.visitFieldInsn(Opcodes.PUTFIELD, internalName(), "lastV", Type.getDescriptor(domainType));
    }
    // for each inlined nested Function field f: if (this.f != null) this.f.invalidateCache();
    // Skipped for the non-propagating invalidateLocalCache variant.
    if (propagate)
    {
      for (var mapping : referencedFunctions.values())
      {
        var skip = new Label();
        loadThisOntoStack(mv);
        mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), mapping.functionName, mapping.functionFieldDescriptor());
        mv.visitJumpInsn(Opcodes.IFNULL, skip);
        loadThisOntoStack(mv);
        mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), mapping.functionName, mapping.functionFieldDescriptor());
        mv.visitMethodInsn(Opcodes.INVOKEINTERFACE, functionInternal, "invalidateCache", "()V", true);
        mv.visitLabel(skip);
      }
    }
    // release the reentrancy flag (shared-cache flag for a cluster, else per-instance)
    if (cached)
    {
      loadThisOntoStack(mv);
      mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "cache", Type.getDescriptor(IndexCache.class));
      mv.visitInsn(Opcodes.ICONST_0);
      mv.visitFieldInsn(Opcodes.PUTFIELD, Type.getInternalName(IndexCache.class), "invalidating", "Z");
    }
    else
    {
      loadThisOntoStack(mv);
      mv.visitInsn(Opcodes.ICONST_0);
      mv.visitFieldInsn(Opcodes.PUTFIELD, internalName(), "invalidatingCache", "Z");
    }
    mv.visitInsn(Opcodes.RETURN);
    mv.visitMaxs(0, 0);
    mv.visitEnd();
    return classVisitor;
  }


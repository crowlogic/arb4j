  public String registerIntermediateVariable(String intermediateVarName, Class<?> type, boolean initialize)
  {
    assert intermediateVarName != null : "intermediateVarName shan't be null where type=" + type + "initialize=" + initialize;
    // assert !type.isInterface() : "cannot instantiate interface " + type;

    if (!isGeneratedFunctional())
    {
      var newIntermediateVariable = new IntermediateVariable<>(this,
                                                               intermediateVarName,
                                                               type,
                                                               initialize);
      if (intermediateVariables.containsKey(intermediateVarName))
      {
        throw new CompilerException(String.format("an intermediate variable named %s already exists", intermediateVarName));
      }

      intermediateVariables.put(intermediateVarName, newIntermediateVariable);

      assert intermediateVarName != null : "registerIntermediateVariable returned null for intermediateVarName=" + intermediateVarName;

      return intermediateVarName;
    }
    else
    {
      // if the coDomain is a functon, then it will be an interface and thus
      // no intermediate variables will be declared in this class, instead they are
      // declared in the class that will be returned
      return null;
    }

  }

  public Expression<D, C, F> registerReferencedFunction(String referencedFunctionName, FunctionMapping<?, ?, ?> referenceFunctionMapping)
  {
    if (BUILTIN_FUNCTION_NAMES.contains(referencedFunctionName))
    {
      throw new CompilerException("cannot register a user-defined function named '"
                                  + referencedFunctionName
                                  + "': that name is reserved for the built-in "
                                  + referencedFunctionName
                                  + ". Rename the function.");
    }
    referencedFunctions.put(referencedFunctionName, referenceFunctionMapping);
    return this;
  }


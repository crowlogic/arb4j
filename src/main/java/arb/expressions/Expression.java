  public void throwUnexpectedCharacterException(String msg, Character... which)
  {
    StringBuilder sb = new StringBuilder();
    for (char ch : which)
    {
      sb.append(String.format("'%s'(%d)", ch, (int) ch));
    }
    String result = sb.toString() + " of len " + which.length;
    throw new CompilerException(format("Expecting %s %s at position %d but got char '%c' instead in expr='%s', remaining=%s%s\n",
                                       which.length > 1 ? "one of" : "",
                                       result,
                                       position,
                                       character == 0 ? '0' : character,
                                       getExpression(),
                                       remaining(),
                                       msg != null ? " syntax=\"" + msg : "",
                                       implementedInterfaces));
  }

  @Override
  public String toString()
  {

    String str = null;
    if (Object.class.equals(domainType))
    {
      str = rootNode != null ? rootNode.toString() : getExpression();
    }
    if (rootNode != null)
    {
      if (getIndependentVariable() == null)
      {
        str = rootNode.toString();
      }
      else
      {
        str = String.format("%s➔%s", getIndependentVariable().getName(), rootNode.toString());
      }
    }
    else
    {
      str = getExpression();
    }
    if (functionName != null && !functionName.startsWith("_") && !functionName.startsWith("operand"))
    {
      str = String.format("%s:%s", functionName, getExpression());
    }
    if (str == null || "null".equals(str))
    {
      updateStringRepresentation();
      str = expression;
    }
    return str;
  }


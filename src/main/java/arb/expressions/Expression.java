  /**
   * Generates the {@code evaluateStaticSubexpressions} method on the generated
   * class. This method has the same parameter layout as {@code evaluate()}
   * ({@code Object, int, int, Object}) so that {@code ILOAD 3} resolves to
   * {@code bits}. It computes every {@link StaticNode} delegate, storing the
   * result into the corresponding field, and then writes {@code bits} into the
   * {@code staticPrecision} field.
   * <p>
   * Called from {@code evaluate()} whenever {@code bits > staticPrecision}.
   */
  protected ClassVisitor generateStaticEvaluationMethod(ClassVisitor classVisitor)
  {
    if (!hasStaticNodes)
    {
      return classVisitor;
    }
    var mv = classVisitor.visitMethod(Opcodes.ACC_PUBLIC, EVALUATE_STATIC_SUBEXPRESSIONS, Compiler.evaluationMethodDescriptor, null, null);
    mv.visitCode();
    try
    {
      generationContext = GenerationContext.StaticEvaluation;
      generateStaticSubexpressionComputations(mv);

      // this.staticPrecision = bits
      loadThisOntoStack(mv);
      Compiler.loadBitsParameterOntoStack(mv);
      mv.visitFieldInsn(Opcodes.PUTFIELD, internalName(), "staticPrecision", "I");
    }
    finally
    {
      generationContext = GenerationContext.Evaluation;
    }
    mv.visitInsn(Opcodes.ACONST_NULL);
    mv.visitInsn(Opcodes.ARETURN);
    mv.visitMaxs(0, 0);
    mv.visitEnd();
    return classVisitor;
  }

  /**
   * Emits a check at the top of {@code evaluate()} that calls
   * {@code evaluateStaticSubexpressions(input, order, bits, result)} when
   * {@code bits > this.staticPrecision}.
   */
  protected void generateStaticPrecisionCheck(MethodVisitor mv)
  {
    String internalName = internalName();
    Label  skipLabel    = new Label();

    // if (this.staticPrecision >= bits) goto skipLabel
    loadThisOntoStack(mv);
    mv.visitFieldInsn(Opcodes.GETFIELD, internalName, "staticPrecision", "I");
    Compiler.loadBitsParameterOntoStack(mv); // ILOAD 3
    mv.visitJumpInsn(Opcodes.IF_ICMPGE, skipLabel);

    // this.evaluateStaticSubexpressions(input, order, bits, result)
    loadThisOntoStack(mv);
    mv.visitVarInsn(Opcodes.ALOAD, 1); // input
    mv.visitVarInsn(Opcodes.ILOAD, 2); // order
    mv.visitVarInsn(Opcodes.ILOAD, 3); // bits
    mv.visitVarInsn(Opcodes.ALOAD, 4); // result
    mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL, internalName, EVALUATE_STATIC_SUBEXPRESSIONS, Compiler.evaluationMethodDescriptor, false);
    mv.visitInsn(Opcodes.POP); // discard return value

    designateLabel(mv, skipLabel);
  }

  /**
   * Emits bytecode that computes each {@link StaticNode} and stores the result
   * into its field. Called inside
   * {@link #generateStaticEvaluationMethod(ClassVisitor)} where
   * {@link #generationContext} is {@link GenerationContext#StaticEvaluation}.
   * <p>
   * Each delegate's {@code generate()} leaves the computed result on the stack.
   * This method then stores that reference into the StaticNode's own field via
   * {@code PUTFIELD}.
   */
  @SuppressWarnings(
  { "rawtypes", "unchecked" })
  protected void generateStaticSubexpressionComputations(MethodVisitor mv)
  {
    String internalName = internalName();
    rootNode.nodeStream().filter(node -> node instanceof StaticNode).map(node -> (StaticNode) node).forEach(staticNode ->
    {
      boolean wasRootNode = staticNode.delegate.isRootNode;
      staticNode.delegate.isRootNode = false;
      staticNode.generate(mv, staticNode.type());
      staticNode.delegate.isRootNode = wasRootNode;
      // stack: [result]
      // store into this.fieldName: need this under result
      loadThisOntoStack(mv);
      mv.visitInsn(Opcodes.SWAP);
      mv.visitFieldInsn(Opcodes.PUTFIELD, internalName, staticNode.fieldName, staticNode.type().descriptorString());
    });
  }

  /**
   * Emit the Taylor series evaluation path for order > 1 in the generated
   * evaluate() method. When the order parameter exceeds 1, this code:
   * <ol>
   * <li>Grows the derivativeCache by calling derivative() on the last entry</li>
   * <li>Loops k = 0..order-1, evaluating each cached derivative at (t, 1, bits)
   * into result.get(k)</li>
   * <li>Divides by k! for k >= 2 to produce Taylor coefficients</li>
   * </ol>
   * <p>
   * Local variable layout for the Taylor path:
   * </p>
   * 
   * <pre>
   *   slot 5: k (loop counter)
   *   slot 6: factorial (long, occupies slots 6-7)
   *   slot 8: element (Object, result.get(k))
   * </pre>
   */
  protected void generateTaylorSeriesPath(MethodVisitor mv, Label taylorLabel)
  {
    mv.visitLabel(taylorLabel);

    // --- Lazy-init derivativeCache if null (when initialize() was not called) ---
    loadThisOntoStack(mv);
    mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "derivativeCache", arrayListDescriptor);
    Label cacheNotNull = new Label();
    mv.visitJumpInsn(Opcodes.IFNONNULL, cacheNotNull);
    loadThisOntoStack(mv);
    mv.visitTypeInsn(Opcodes.NEW, arrayListInternal);
    mv.visitInsn(Opcodes.DUP);
    mv.visitMethodInsn(Opcodes.INVOKESPECIAL, arrayListInternal, "<init>", "()V", false);
    mv.visitFieldInsn(Opcodes.PUTFIELD, internalName(), "derivativeCache", arrayListDescriptor);
    mv.visitLabel(cacheNotNull);

    // --- Ensure derivativeCache[0] = fresh instance (not 'this'!) ---
    // Seeding with 'this' would cause re-entrant evaluate() when the loop calls
    // derivativeCache.get(0).evaluate(...) while the outer evaluate() is still running.
    // Instead allocate a new instance of the same class, share the context, and seed it.
    // if (derivativeCache.isEmpty()) derivativeCache.add(new SameClass(context))
    loadThisOntoStack(mv);
    mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "derivativeCache", arrayListDescriptor);
    mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL, arrayListInternal, "size", "()I", false);
    Label cacheNotEmpty = new Label();
    mv.visitJumpInsn(Opcodes.IFNE, cacheNotEmpty);
    loadThisOntoStack(mv);
    mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "derivativeCache", arrayListDescriptor); // [list]
    mv.visitTypeInsn(Opcodes.NEW, internalName());                                               // [list, uninit]
    mv.visitInsn(Opcodes.DUP);                                                                   // [list, uninit, uninit]
    mv.visitMethodInsn(Opcodes.INVOKESPECIAL, internalName(), "<init>", "()V", false);           // [list, fresh]
    mv.visitInsn(Opcodes.DUP);                                                                   // [list, fresh, fresh]
    loadThisOntoStack(mv);                                                                        // [list, fresh, fresh, this]
    mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "context", Type.getDescriptor(Context.class)); // [list, fresh, fresh, ctx]
    mv.visitFieldInsn(Opcodes.PUTFIELD, internalName(), "context", Type.getDescriptor(Context.class)); // [list, fresh]
    mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL, arrayListInternal, "add", "(Ljava/lang/Object;)Z", false); // [bool]
    mv.visitInsn(Opcodes.POP); // discard boolean return
    mv.visitLabel(cacheNotEmpty);

    // --- long factorial = 1; ---
    // slot 5 = k (int), slot 6-7 = factorial (long), slot 8 = element (Object)
    int kSlot         = 5;
    int factorialSlot = 6;
    int elementSlot   = 8;

    mv.visitInsn(Opcodes.LCONST_1);
    mv.visitVarInsn(Opcodes.LSTORE, factorialSlot);

    // --- int k = 0; ---
    mv.visitInsn(Opcodes.ICONST_0);
    mv.visitVarInsn(Opcodes.ISTORE, kSlot);

    Label loopTop = new Label();
    Label loopEnd = new Label();

    // --- loopTop: if (k >= order) goto loopEnd ---
    mv.visitLabel(loopTop);
    mv.visitVarInsn(Opcodes.ILOAD, kSlot);
    mv.visitVarInsn(Opcodes.ILOAD, 2); // order
    mv.visitJumpInsn(Opcodes.IF_ICMPGE, loopEnd);

    // --- if (k > 0) factorial *= k ---
    mv.visitVarInsn(Opcodes.ILOAD, kSlot);
    Label skipFactorial = new Label();
    mv.visitJumpInsn(Opcodes.IFLE, skipFactorial);
    mv.visitVarInsn(Opcodes.LLOAD, factorialSlot);
    mv.visitVarInsn(Opcodes.ILOAD, kSlot);
    mv.visitInsn(Opcodes.I2L);
    mv.visitInsn(Opcodes.LMUL);
    mv.visitVarInsn(Opcodes.LSTORE, factorialSlot);
    mv.visitLabel(skipFactorial);

    // --- Grow derivativeCache while cache.size() <= k ---
    Label growCheck = new Label();
    Label growDone  = new Label();
    mv.visitLabel(growCheck);
    loadThisOntoStack(mv);
    mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "derivativeCache", arrayListDescriptor);
    mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL, arrayListInternal, "size", "()I", false);
    mv.visitVarInsn(Opcodes.ILOAD, kSlot);
    mv.visitJumpInsn(Opcodes.IF_ICMPGT, growDone); // size > k means cache has entry k
    // prev = cache.get(cache.size() - 1)
    loadThisOntoStack(mv);
    mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "derivativeCache", arrayListDescriptor);
    mv.visitInsn(Opcodes.DUP); // stack: cache, cache
    mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL, arrayListInternal, "size", "()I", false);
    mv.visitInsn(Opcodes.ICONST_1);
    mv.visitInsn(Opcodes.ISUB); // stack: cache, size-1
    mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL, arrayListInternal, "get", "(I)Ljava/lang/Object;", false);
    mv.visitTypeInsn(Opcodes.CHECKCAST, functionInternal);
    // call prev.derivative()
    mv.visitMethodInsn(Opcodes.INVOKEINTERFACE, functionInternal, "derivative", "()L" + functionInternal + ";", true);
    // stack: newDerivative; push cache again for add
    // need cache ref on stack before the value
    // Reload cache, swap
    loadThisOntoStack(mv);
    mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "derivativeCache", arrayListDescriptor);
    mv.visitInsn(Opcodes.SWAP); // stack: cache, newDerivative
    mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL, arrayListInternal, "add", "(Ljava/lang/Object;)Z", false);
    mv.visitInsn(Opcodes.POP); // discard boolean
    mv.visitJumpInsn(Opcodes.GOTO, growCheck);
    mv.visitLabel(growDone);

    // --- fk = derivativeCache.get(k) ---
    loadThisOntoStack(mv);
    mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "derivativeCache", arrayListDescriptor);
    mv.visitVarInsn(Opcodes.ILOAD, kSlot);
    mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL, arrayListInternal, "get", "(I)Ljava/lang/Object;", false);
    mv.visitTypeInsn(Opcodes.CHECKCAST, functionInternal);
    // stack: fk (Function)

    // --- element = result.get(k) ---
    mv.visitVarInsn(Opcodes.ALOAD, 4); // result
    mv.visitTypeInsn(Opcodes.CHECKCAST, fieldInternal);
    mv.visitVarInsn(Opcodes.ILOAD, kSlot);
    mv.visitMethodInsn(Opcodes.INVOKEINTERFACE, fieldInternal, "get", "(I)Ljava/lang/Object;", true);
    mv.visitVarInsn(Opcodes.ASTORE, elementSlot);

    // --- fk.evaluate(t, 1, bits, element) ---
    // stack already has fk; push args
    mv.visitVarInsn(Opcodes.ALOAD, 1); // t
    mv.visitInsn(Opcodes.ICONST_1); // order = 1
    mv.visitVarInsn(Opcodes.ILOAD, 3); // bits
    mv.visitVarInsn(Opcodes.ALOAD, elementSlot); // element
    mv.visitMethodInsn(Opcodes.INVOKEINTERFACE, functionInternal, "evaluate", Compiler.evaluationMethodDescriptor, true);
    mv.visitInsn(Opcodes.POP); // discard return value

    // --- if (k >= 2) element.div((int)factorial, bits, element) ---
    mv.visitVarInsn(Opcodes.ILOAD, kSlot);
    mv.visitInsn(Opcodes.ICONST_2);
    Label skipDiv = new Label();
    mv.visitJumpInsn(Opcodes.IF_ICMPLT, skipDiv);
    mv.visitVarInsn(Opcodes.ALOAD, elementSlot);
    mv.visitTypeInsn(Opcodes.CHECKCAST, fieldInternal);
    mv.visitVarInsn(Opcodes.LLOAD, factorialSlot);
    mv.visitInsn(Opcodes.L2I); // truncate to int for div(int,int,X)
    mv.visitVarInsn(Opcodes.ILOAD, 3); // bits
    mv.visitVarInsn(Opcodes.ALOAD, elementSlot);
    mv.visitTypeInsn(Opcodes.CHECKCAST, fieldInternal); // cast Object to Field
    mv.visitMethodInsn(Opcodes.INVOKEINTERFACE, fieldInternal, "div", "(IIL" + fieldInternal + ";)L" + fieldInternal + ";", true);
    mv.visitInsn(Opcodes.POP); // discard return
    mv.visitLabel(skipDiv);

    // --- k++ ---
    mv.visitIincInsn(kSlot, 1);
    mv.visitJumpInsn(Opcodes.GOTO, loopTop);

    // --- return result ---
    mv.visitLabel(loopEnd);
    mv.visitVarInsn(Opcodes.ALOAD, 4); // result
    mv.visitInsn(Opcodes.ARETURN);
  }

  /**
   * TODO: need to generate instructions so that the toString() uses String.format
   * to include the value (only if it was part of the independent variable because
   * thats the only its valued is fixed for the duration of the instance of the
   * class, if its just a Context variable then it can change between invocations
   *
   * @param classVisitor
   * @return
   */
  protected ClassVisitor generateToStringMethod(ClassVisitor classVisitor)
  {
    var methodVisitor = classVisitor.visitMethod(ACC_PUBLIC, "toString", Compiler.getMethodDescriptor(String.class), null, null);
    methodVisitor.visitCode();
    Compiler.annotateWithOverride(methodVisitor);

    // Collect variables that are fields on this class with runtime-varying values.
    // 1. Upstream input variables (ancestor scope vars, excluding immediate
    // parent's independent variable which the predicate filters out)
    List<Entry<String, VariableNode<D, C, F>>> runtimeVars = new ArrayList<>();
    upstreamInputVariableEntryStream().forEach(runtimeVars::add);

    // 2. Walk the upstream chain: each ancestor's independent variable is
    // propagated by value to this class as a field, but the predicate in
    // upstreamInputVariableEntryStream() explicitly excludes it.
    // Include it here since the field holds a concrete value at runtime.
    Expression<?, ?, ?> ancestor = upstreamExpression;
    while (ancestor != null)
    {
      if (ancestor.getIndependentVariable() != null)
      {
        String name = ancestor.getIndependentVariable().getName();
        var    ref  = referencedVariables.get(name);
        if (ref != null && runtimeVars.stream().noneMatch(e -> e.getKey().equals(name)))
        {
          runtimeVars.add(Map.entry(name, ref));
        }
      }
      ancestor = ancestor.upstreamExpression;
    }

    String namePrefix = (functionName != null && !functionName.isEmpty()) ? functionName + ":" : "";
    updateStringRepresentation();
    String bodyExpr = rootNode != null ? rootNode.toString() : getExpression();
    String arrow    = (getIndependentVariable() == null || bodyExpr.contains("➔")) ? "" : (getIndependentVariable().getName() + "➔");

    if (runtimeVars.isEmpty())
    {
      String boundExpr = rootNode != null ? rootNode.toStringBound(Collections.emptyMap()) : getExpression();
      String staticStr = namePrefix + arrow + boundExpr;

      if (Expression.trace)
      {
        log.debug("generateToStringMethod(): static path string='{}'", staticStr);
      }

      methodVisitor.visitLdcInsn(staticStr);
      Compiler.generateReturnFromMethod(methodVisitor);
      return classVisitor;
    }

    // Build a 1-based index map (name → position in args array) so every
    // occurrence of variable x emits the same indexed specifier "%N$s" and
    // they all share one positional argument. Without this, an expression
    // that mentions x twice produces two "%s" placeholders but only one
    // argument, yielding a MissingFormatArgumentException from String.format.
    // The indices match the order in which entries are added to
    // the Object[] args array below.
    List<String>                   matchedNames    = new ArrayList<>();
    List<VariableNode<D, C, F>>    matchedNodes    = new ArrayList<>();
    Map<String, java.lang.Integer> variableIndices = new HashMap<>();
    int                            nextIndex       = 1;
    for (Entry<String, VariableNode<D, C, F>> entry : runtimeVars)
    {
      matchedNames.add(entry.getKey());
      matchedNodes.add(entry.getValue());
      variableIndices.put(entry.getKey(), nextIndex++);
    }

    // toStringBound(variableIndices) emits indexed specifiers so repeated
    // occurrences of the same variable share a single argument slot.
    String boundExpr    = rootNode != null ? rootNode.toStringBound(variableIndices) : getExpression();
    String formatString = namePrefix + arrow + boundExpr;

    if (Expression.trace)
    {
      log.debug("generateToStringMethod(): dynamic path formatString='{}' vars={}", formatString, matchedNames);
    }

    methodVisitor.visitLdcInsn(formatString);

    methodVisitor.visitLdcInsn(matchedNames.size());
    methodVisitor.visitTypeInsn(ANEWARRAY, Type.getInternalName(Object.class));

    for (int i = 0; i < matchedNames.size(); i++)
    {
      duplicateTopOfTheStack(methodVisitor);
      methodVisitor.visitLdcInsn(i);
      loadThisAndFieldOntoStack(methodVisitor, matchedNames.get(i), matchedNodes.get(i).type());
      // Use String.valueOf(Object) rather than Object.toString(): the field
      // can be null when toString() is invoked before injection, e.g. when
      // a trace-log statement in Context.registerFunctionMapping formats a
      // freshly-constructed instance during parse-time subexpression
      // registration. valueOf is the null-safe equivalent and returns the
      // string "null" rather than throwing NullPointerException.
      invokeStaticMethod(methodVisitor, String.class, "valueOf", String.class, Object.class);
      methodVisitor.visitInsn(AASTORE);
    }

    invokeStaticMethod(methodVisitor, String.class, "format", String.class, String.class, Object[].class);

    Compiler.generateReturnFromMethod(methodVisitor);
    return classVisitor;
  }


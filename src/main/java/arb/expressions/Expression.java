  {
    assert !variableNode.equals(getIndependentVariable()) : "independent variables of this or any upstream expression are always assumed to be referenced. this is for context variables only: not adding "
                                                            + variableNode
                                                            + " to the referencedVariables of "
                                                            + this.toStringExtended();
    return referencedVariables.put(variableNode.reference.name, variableNode);
  }

  @SuppressWarnings("hiding")
  public <A, B, Q extends Function<? extends A, ? extends B>> FunctionMapping<A, B, Q> registerSubexpression(Expression<A, B, Q> expr)
  {
    if (expr.upstreamExpression == null)
    {
      expr.upstreamExpression = this;
      subExpressions.add(expr);
    }
    else if (expr.upstreamExpression != this && !subExpressions.contains(expr))
    {
      subExpressions.add(expr);
    }

    if (context == null)
    {
      context = new Context();
    }
    expr.context = context;

    return context.registerFunctionMapping(expr.className, expr.instantiate(), expr.domainType, expr.coDomainType, Function.class, true, expr, null);
  }


  /**
   * Recursively walks a {@link MultiplicationNode}/{@link DivisionNode} chain
   * collecting every non-Mul/Div operand along with its accumulated
   * numerator/denominator sign. The right operand of a {@link DivisionNode} flips
   * the sign; all other descents preserve it. Stops descending at a
   * {@link StaticNode} so its cached subtree is treated as an atomic factor.
   */
  @SuppressWarnings(
  { "rawtypes", "unchecked" })
  private void collectMulDivOperands(Node node, int sign, List<FactorRole> out)
  {
    if (node instanceof MultiplicationNode mul)
    {
      collectMulDivOperands(mul.left, sign, out);
      collectMulDivOperands(mul.right, sign, out);
    }
    else if (node instanceof DivisionNode div)
    {
      collectMulDivOperands(div.left, sign, out);
      collectMulDivOperands(div.right, -sign, out);
    }
    else
    {
      out.add(new FactorRole(node,
                             sign));
    }
  }

  public Expression<D, C, F> compile()
  {
    assert !className.isEmpty() : "className is empty";
    if (compiledClass != null)
    {
      return this;
    }
    // #1027 fix: prevent re-entrant compile() from running optimizeAndGenerate
    // a second time before the first invocation finishes. This happens when
    // {@link Compiler#loadFunctionClass} calls {@code loader.loadClass(...)},
    // which triggers {@link ExpressionClassLoader#findClass} — and the
    // findClass path can resolve to this same Expression via the
    // {@code functionMapping} branch (because {@code compiledClass} hasn't
    // been assigned yet), which calls {@code FunctionMapping.instantiate} →
    // {@code expression.compile()} → a second {@code optimizeAndGenerate()}
    // that re-runs {@code declareVariables} on this same Expression instance.
    // The {@code variablesDeclared} assert at
    // {@link #declareVariables} catches this; the proper fix is to
    // short-circuit re-entrant compile() calls so the recursion settles on
    // the partially-built state of the in-progress compile.
    if (compileInProgress)
    {
      return this;
    }
    compileInProgress = true;
    if (trace) log.debug("#{}: compileInProgress = {}", System.identityHashCode(this), compileInProgress);
    try
    {
      if (context == null)
      {
        context = new Context();
        if (trace) log.debug("#{}: context = {}", System.identityHashCode(this), context);
      }

      if (trace)
      {
        log.debug(String.format("#%s: compile(expression=%s, className=%s, context=%s)\n", System.identityHashCode(this), getExpression(), className, context));
      }
      if (instructions == null)
      {
        optimizeAndGenerate();
      }
      assert context != null : "context is null for "
                               + this
                               + " and superExpression="
                               + upstreamExpression
                               + " superExpression.context="
                               + upstreamExpression.context;
      assert !className.isEmpty() : "className is empty";
      // #1027 fix: only call loadFunctionClass here if optimizeAndGenerate did
      // not already set compiledClass. optimizeAndGenerate's path sets it via
      // its own loadFunctionClass at ~line 4327; without this guard, the
      // generated bytecode was being defined twice per class.
      if (compiledClass == null)
      {
        compiledClass = loadFunctionClass(internalName(), instructions, context);
        if (trace) log.debug("#{}: compiledClass = {}", System.identityHashCode(this), compiledClass);
      }
      return this;
    }
    finally
    {
      compileInProgress = false;
      if (trace) log.debug("#{}: compileInProgress = {}", System.identityHashCode(this), compileInProgress);
    }
  }

  /**
   * Returns true if following the reference graph from {@code from} eventually
   * reaches a function named {@code target}. Used to detect all cycle lengths
   * in a mutually-recursive cluster at expression-compile time.
   */
  static boolean isTransitivelyReachable(FunctionMapping<?, ?, ?> from, String target, Set<String> visited)
  {
    if (from == null || from.expression == null)
    {
      return false;
    }
    for (var entry : from.expression.getReferencedFunctions().entrySet())
    {
      String name = entry.getKey();
      if (name.equals(target))
      {
        return true;
      }
      if (visited.add(name) && isTransitivelyReachable(entry.getValue(), target, visited))
      {
        return true;
      }
    }
    return false;
  }

  protected void constructReferencedFunctionInstanceIfItIsNull(MethodVisitor mv, FunctionMapping<?, ?, ?> mapping)
  {
    assert mapping != null : "mapping shan't be null";
    boolean isSelfRef        = mapping.functionName != null && functionName != null && functionName.equals(mapping.functionName);
    boolean hasSomething     = mapping.expression != null || mapping.instance != null;
    // A forward-declared peer: ctx.declare("f",...) was called but express("f",...)
    // hasn't run yet when this expression was compiled. The concrete class is
    // unknown at compile time, so we emit a runtime call to allocatePeer
    // rather than a bytecode `new`.
    boolean isForwardDeclared = !hasSomething;

    if (!isSelfRef && (hasSomething || isForwardDeclared))
    {
      String fieldDescriptor    = mapping.functionFieldDescriptor();
      String contextTypeDesc    = Type.getDescriptor(Context.class);
      var    alreadyInitialized = new Label();

      if (isForwardDeclared)
      {
        // Forward-declared peer (declared but not yet expressed at compile time).
        // Emit: if (this.f == null) { this.f = (FType) this.context.allocatePeer("f"); }
        // At runtime, allocatePeer looks up the now-instantiated class and
        // returns an instance (own `evaluating` flag, shared context & cache).
        loadThisOntoStack(mv).visitFieldInsn(GETFIELD, internalName(), mapping.functionName, fieldDescriptor);
        mv.visitJumpInsn(Opcodes.IFNONNULL, alreadyInitialized);
        loadThisOntoStack(mv); // for PUTFIELD
        loadThisOntoStack(mv);
        mv.visitFieldInsn(GETFIELD, internalName(), "context", contextTypeDesc);
        mv.visitLdcInsn(mapping.functionName);
        mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL,
                           Type.getInternalName(Context.class),
                           "allocatePeer",
                           "(Ljava/lang/String;)Larb/functions/Function;",
                           false);
        mv.visitTypeInsn(Opcodes.CHECKCAST, Type.getInternalName(mapping.functionClass));
        putField(mv, internalName(), mapping.functionName, fieldDescriptor);
      }
      else
      {
        // Bytecode-only path: use the mapping's already-known generated-class
        // internal name. ASM's new/invokespecial accept name strings; resolving
        // the Class<?> here would force eager compilation of the dependency
        // and break mutual recursion across mappings. The class is resolved
        // lazily by the JVM when the opcode first executes — by which point the
        // outer compile chain has defined every class in the recursive cluster.
        String typeInternalName = mapping.functionInternalName();
        // Detect if the peer participates in a reference cycle with this
        // expression (any cycle length, not just 2). For all such peers we must
        // allocate a distinct instance so each evaluation level owns its own
        // `evaluating` flag and the re-entrancy guard cannot fire. Σ/Π operand
        // sub-expressions carry a
        // null functionName but are referenced by their owner under className(),
        // so fall back to className() for the cycle test — otherwise a recursive
        // reference nested inside a sum operand is missed and aliases the same
        // instance, tripping the guard.
        String  ownerName           = functionName != null ? functionName : className();
        boolean isMutuallyRecursive = isTransitivelyReachable(mapping, ownerName, new HashSet<>());
        var     needsAllocation     = new Label();

        // 1. if (this.<field> != null) skip → field already populated, done.
        loadThisOntoStack(mv).visitFieldInsn(GETFIELD, internalName(), mapping.functionName, fieldDescriptor);
        mv.visitJumpInsn(Opcodes.IFNONNULL, alreadyInitialized);

        if (isMutuallyRecursive)
        {
          // Recursive peer: allocate via allocatePeer so the instance
          // shares the context singleton's IndexCache — without this, each
          // evaluation level gets its own empty cache and q(n) is recomputed
          // O(2^n) times instead of being memoized.
          // if (this.g == null) { this.g = (GType) context.allocatePeer("g"); }
          loadThisOntoStack(mv); // for PUTFIELD
          loadThisOntoStack(mv);
          mv.visitFieldInsn(GETFIELD, internalName(), "context", contextTypeDesc);
          mv.visitLdcInsn(mapping.functionName);
          mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL,
                             Type.getInternalName(Context.class),
                             "allocatePeer",
                             "(Ljava/lang/String;)Larb/functions/Function;",
                             false);
          mv.visitTypeInsn(Opcodes.CHECKCAST, typeInternalName);
          putField(mv, internalName(), mapping.functionName, fieldDescriptor);
        }
        else
        {
          // 2. Try the context's canonical instance (only for non-recursive peers):
          // Function f = this.context.lookupFunctionInstance("<name>");
          // if (f != null) { this.<field> = (FieldType) f; goto done; }
          loadThisOntoStack(mv);
          loadThisOntoStack(mv);
          mv.visitFieldInsn(GETFIELD, internalName(), "context", contextTypeDesc);
          mv.visitLdcInsn(mapping.functionName);
          mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL,
                             Type.getInternalName(Context.class),
                             "lookupFunctionInstance",
                             "(Ljava/lang/String;)Larb/functions/Function;",
                             false);
          duplicateTopOfTheStack(mv);
          mv.visitJumpInsn(Opcodes.IFNULL, needsAllocation);
          // Stack: this, function. Cast and store.
          mv.visitTypeInsn(Opcodes.CHECKCAST, typeInternalName);
          putField(mv, internalName(), mapping.functionName, fieldDescriptor);
          mv.visitJumpInsn(Opcodes.GOTO, alreadyInitialized);

          // 3. Fallback when context has no instance yet.
          mv.visitLabel(needsAllocation);
          mv.visitInsn(Opcodes.POP); // discard the dup'd null
          mv.visitInsn(Opcodes.POP); // discard `this` for the PUTFIELD
          loadThisOntoStack(mv);
          generateNewObjectInstruction(mv, typeInternalName);
          duplicateTopOfTheStack(mv);
          invokeDefaultConstructor(mv, typeInternalName);
          putField(mv, internalName(), mapping.functionName, fieldDescriptor);
          // Propagate the parent's context field.
          loadThisOntoStack(mv);
          mv.visitFieldInsn(GETFIELD, internalName(), mapping.functionName, fieldDescriptor);
          loadThisOntoStack(mv);
          mv.visitFieldInsn(GETFIELD, internalName(), "context", contextTypeDesc);
          mv.visitFieldInsn(PUTFIELD, typeInternalName, "context", contextTypeDesc);
        }
      }

      mv.visitLabel(alreadyInitialized);
    }
  }


  protected Expression<Object, Object, Function<?, ?>> newFunctionalExpression()
  {
    Class<?>                        funcDomain   = null;
    Class<?>                        funcCoDomain = null;
    Class<? extends Function<?, ?>> funcClass    = null;

    if (RealFunction.class.equals(coDomainType))
    {
      funcDomain   = Real.class;
      funcCoDomain = Real.class;
      funcClass    = RealFunction.class;
    }
    else if (RealFunctional.class.equals(coDomainType))
    {
      funcDomain   = Real.class;
      funcCoDomain = RealFunction.class;
      funcClass    = RealFunctional.class;
    }
    else if (ComplexFunctional.class.equals(coDomainType))
    {
      funcDomain   = Complex.class;
      funcCoDomain = ComplexFunction.class;
      funcClass    = ComplexFunctional.class;
    }
    else if (ComplexFunction.class.equals(coDomainType))
    {
      funcDomain   = Complex.class;
      funcCoDomain = Complex.class;
      funcClass    = ComplexFunction.class;
    }
    else if (RealToComplexFunction.class.equals(coDomainType))
    {
      funcDomain   = Real.class;
      funcCoDomain = Complex.class;
      funcClass    = RealToComplexFunction.class;
    }
    else if (RealSequence.class.equals(coDomainType))
    {
      funcDomain   = Integer.class;
      funcCoDomain = Real.class;
      funcClass    = RealSequence.class;
    }
    else if (ComplexSequence.class.equals(coDomainType))
    {
      funcDomain   = Integer.class;
      funcCoDomain = Complex.class;
      funcClass    = ComplexSequence.class;
    }
    else if (ComplexPolynomialSequence.class.equals(coDomainType))
    {
      funcDomain   = Integer.class;
      funcCoDomain = ComplexPolynomial.class;
      funcClass    = ComplexPolynomialSequence.class;
    }
    else if (RealPolynomialSequence.class.equals(coDomainType))
    {
      funcDomain   = Integer.class;
      funcCoDomain = RealPolynomial.class;
      funcClass    = RealPolynomialSequence.class;
    }
    else if (ComplexFunctionalSequence.class.equals(coDomainType))
    {
      funcDomain   = Integer.class;
      funcCoDomain = ComplexFunctional.class;
      funcClass    = ComplexFunctionalSequence.class;
    }
    else if (ComplexFunctionalSequenceSequence.class.equals(coDomainType))
    {
      funcDomain   = Integer.class;
      funcCoDomain = ComplexFunctionalSequence.class;
      funcClass    = ComplexFunctionalSequenceSequence.class;
    }
    else
    {
      throw new UnsupportedOperationException("TODO: implement " + coDomainType + " codomain functional");
    }

    var functionalExpression = new Expression<Object, Object, Function<?, ?>>(funcDomain,
                                                                              funcCoDomain,
                                                                              funcClass);
    functionalExpression.upstreamExpression = this;
    // Preserve the parse-time functionalChild (the next arrow level) BEFORE
    // overwriting it with the generate-time functional. The parse-time chain
    // holds the correct per-level independent variables; it must be threaded
    // forward so functional chains of depth >= 3 do not lose the innermost
    // independent variable (issue #1188).
    Expression<?, ?, ?> parseTimeChild = this.functionalChild;
    this.functionalChild                    = functionalExpression;
    if (context == null)
    {
      context = new Context();
    }
    functionalExpression.expression = expression;
    functionalExpression.context    = context;

    // Use the declared variable as the functional expression's independent
    // variable.
    var declaredVariable = placeholderVariable;
    placeholderVariable = null;

    if (declaredVariable != null)
    {
      functionalExpression.setIndependentVariable(declaredVariable.spliceInto(functionalExpression).asVariable());
    }

    // Thread the next arrow level's independent variable forward from the
    // parse-time chain. When functionalExpression is itself a generated
    // functional, its own newFunctionalExpression() reads these to set its
    // child's independent variable, instead of losing it (issue #1188).
    if (parseTimeChild != null && parseTimeChild != functionalExpression)
    {
      @SuppressWarnings("unchecked")
      VariableNode<Object, Object, Function<?, ?>> nextLevelVariable =
                                                                     (VariableNode<Object, Object, Function<?, ?>>) (VariableNode<?, ?, ?>) parseTimeChild.placeholderVariable;
      if (nextLevelVariable != null)
      {
        functionalExpression.placeholderVariable = nextLevelVariable;
      }
      functionalExpression.functionalChild = parseTimeChild.functionalChild;
    }

    rootNode.isRootNode                      = true;

    functionalExpression.className           = className == null ? null : className + "func";
    functionalExpression.functionName        = functionName == null ? null : functionName + "func";

    functionalExpression.rootNode            = rootNode.spliceInto(functionalExpression);
    functionalExpression.rootNode.isRootNode = rootNode.isRootNode;
    // Carry every referenced-function mapping (hand-wired context instance OR
    // compiled peer) over to the functional. The functional is the user-facing
    // generated class that actually executes the body; without these entries
    // it would not declare a field for the referenced function and Phase-2
    // wire-all in its initialize() would have nothing to copy through to inner
    // sum operands. The structural-cycle detector that previously rejected
    // compiled-peer carry-over has been removed (Issue #1005).
    for (var refEntry : referencedFunctions.entrySet())
    {
      var refMapping = refEntry.getValue();
      if (refMapping != null)
      {
        functionalExpression.registerReferencedFunction(refEntry.getKey(), refMapping);
      }
    }
    functionalExpression.updateStringRepresentation();
    return functionalExpression;
  }

  private F newInstance()
  {
    if (compiledClass == null)
    {
      compile();
    }
    try
    {
      // No side-write to the `instance` field here — instantiate() is the
      // only caller that should publish a freshly constructed instance,
      // and only after wiring is complete. Doing the assignment here used
      // to clobber another thread's in-flight instance during concurrent
      // instantiate() calls.
      return compiledClass.getDeclaredConstructor().newInstance();
    }
    catch (Exception e)
    {
      Utensils.wrapOrThrow(e);
      return null;
    }

  }


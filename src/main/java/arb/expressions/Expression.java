  /**
   * TODO: need to generate instructions so that the toString() uses String.format
   * to include the value (only if it was part of the independent variable because
   * thats the only its valued is fixed for the duration of the instance of the
   * class, if its just a Context variable then it can change between invocations
   *
   * @param classVisitor
   * @return
   */
  protected ClassVisitor generateToStringMethod(ClassVisitor classVisitor)
  {
    var methodVisitor = classVisitor.visitMethod(ACC_PUBLIC, "toString", Compiler.getMethodDescriptor(String.class), null, null);
    methodVisitor.visitCode();
    Compiler.annotateWithOverride(methodVisitor);

    // Collect variables that are fields on this class with runtime-varying values.
    // 1. Upstream input variables (ancestor scope vars, excluding immediate
    // parent's independent variable which the predicate filters out)
    List<Entry<String, VariableNode<D, C, F>>> runtimeVars = new ArrayList<>();
    upstreamInputVariableEntryStream().forEach(runtimeVars::add);

    // 2. Walk the upstream chain: each ancestor's independent variable is
    // propagated by value to this class as a field, but the predicate in
    // upstreamInputVariableEntryStream() explicitly excludes it.
    // Include it here since the field holds a concrete value at runtime.
    Expression<?, ?, ?> ancestor = upstreamExpression;
    while (ancestor != null)
    {
      if (ancestor.getIndependentVariable() != null)
      {
        String name = ancestor.getIndependentVariable().getName();
        var    ref  = referencedVariables.get(name);
        if (ref != null && runtimeVars.stream().noneMatch(e -> e.getKey().equals(name)))
        {
          runtimeVars.add(Map.entry(name, ref));
        }
      }
      ancestor = ancestor.upstreamExpression;
    }

    String namePrefix = (functionName != null && !functionName.isEmpty()) ? functionName + ":" : "";
    updateStringRepresentation();
    String bodyExpr = rootNode != null ? rootNode.toString() : getExpression();
    String arrow    = (getIndependentVariable() == null || bodyExpr.contains("➔")) ? "" : (getIndependentVariable().getName() + "➔");

    if (runtimeVars.isEmpty())
    {
      String boundExpr = rootNode != null ? rootNode.toStringBound(Collections.emptyMap()) : getExpression();
      String staticStr = namePrefix + arrow + boundExpr;

      if (Expression.trace)
      {
        log.debug("generateToStringMethod(): static path string='{}'", staticStr);
      }

      methodVisitor.visitLdcInsn(staticStr);
      Compiler.generateReturnFromMethod(methodVisitor);
      return classVisitor;
    }

    // Build a 1-based index map (name → position in args array) so every
    // occurrence of variable x emits the same indexed specifier "%N$s" and
    // they all share one positional argument. Without this, an expression
    // that mentions x twice produces two "%s" placeholders but only one
    // argument, yielding a MissingFormatArgumentException from String.format.
    // The indices match the order in which entries are added to
    // the Object[] args array below.
    List<String>                   matchedNames    = new ArrayList<>();
    List<VariableNode<D, C, F>>    matchedNodes    = new ArrayList<>();
    Map<String, java.lang.Integer> variableIndices = new HashMap<>();
    int                            nextIndex       = 1;
    for (Entry<String, VariableNode<D, C, F>> entry : runtimeVars)
    {
      matchedNames.add(entry.getKey());
      matchedNodes.add(entry.getValue());
      variableIndices.put(entry.getKey(), nextIndex++);
    }

    // toStringBound(variableIndices) emits indexed specifiers so repeated
    // occurrences of the same variable share a single argument slot.
    String boundExpr    = rootNode != null ? rootNode.toStringBound(variableIndices) : getExpression();
    String formatString = namePrefix + arrow + boundExpr;

    if (Expression.trace)
    {
      log.debug("generateToStringMethod(): dynamic path formatString='{}' vars={}", formatString, matchedNames);
    }

    methodVisitor.visitLdcInsn(formatString);

    methodVisitor.visitLdcInsn(matchedNames.size());
    methodVisitor.visitTypeInsn(ANEWARRAY, Type.getInternalName(Object.class));

    for (int i = 0; i < matchedNames.size(); i++)
    {
      duplicateTopOfTheStack(methodVisitor);
      methodVisitor.visitLdcInsn(i);
      loadThisAndFieldOntoStack(methodVisitor, matchedNames.get(i), matchedNodes.get(i).type());
      generateVirtualMethodInvocation(methodVisitor, Object.class, "toString", String.class);
      methodVisitor.visitInsn(AASTORE);
    }

    invokeStaticMethod(methodVisitor, String.class, "format", String.class, String.class, Object[].class);

    Compiler.generateReturnFromMethod(methodVisitor);
    return classVisitor;
  }

  protected String getStringRepresentation()
  {
    String name = (functionName != null) ? (functionName + ":") : "";
    updateStringRepresentation();
    String arrow  = getExpression().contains("➔") || getIndependentVariable() == null ? "" : (getIndependentVariable().getName() + "➔");
    String string = String.format("%s%s%s", name, arrow, getExpression());

    if (Expression.trace)
    {
      log.debug("generateToStringMethod(): functionName='{}' independentVariable='{}' name='{}' arrow='{}' string='{}'",
                functionName,
                getIndependentVariable(),
                name,
                arrow,
                string);
    }
    return string;
  }


  public boolean thisOrAnyUpstreamExpressionHasFunctionalCodomain()
  {
    Set<Expression<?, ?, ?>> visited = Collections.newSetFromMap(new IdentityHashMap<>());
    Expression<?, ?, ?>      cursor  = this;
    while (cursor != null)
    {
      if (!visited.add(cursor))
      {
        throw new CompilerException("Cycle detected in superExpression chain starting at #"
                                    + System.identityHashCode(this)
                                    + "="
                                    + toString()
                                    + "; repeated node: #"
                                    + System.identityHashCode(cursor)
                                    + "="
                                    + toString());
      }
      if (cursor.coDomainType != null && (cursor.coDomainType.isInterface() || Function.class.isAssignableFrom(cursor.coDomainType)))
      {
        return true;
      }
      cursor = cursor.upstreamExpression;
    }
    return false;
  }


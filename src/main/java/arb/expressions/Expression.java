  public boolean enableLommelPolynomials = true;

  protected Node<D, C, F> resolveFunction(VariableReference<D, C, F> reference)
  {
    if (reference.is("R") && (context == null || (context != null && !context.disableLommelPolynomials)))
    {
      return new LommelPolynomialNode<>(this);
    }

    switch (reference.name)
    {
    case "int":
      return new IntegralNode<>(this,
                                true);
    case "nint":
      return new NumericalIntegralNode<>(this);
    case "sum":
      return new SumNode<>(this,
                           true);
    case "ℰ":
    case "MittagLeffler":
    case "mittagleffler":
      return new MittagLefflerFunctionNode<>(this);
    case "Z":
      return new HardyZJetNode<>(this);
    case "ϑ":
    case "vartheta":
      return new RiemannSiegelThetaJetNode<>(this);
    case "ζ":
    case "zeta":
      return new ZetaJetNode<>(this);

    case "when":
      return new WhenNode<>(this);
    case "fracdiff":
      return new CaputoFractionalDerivativeNode<>(this);
    case "fracint":
      return new RiemannLiouvilleFractionalIntegralNode<>(this);
    case "diff":
      return new DerivativeNode<>(this,
                                  true);
    case "lim":
    case "limit":
      return new LimitNode<>(this);
    case "J":
      return new BesselFunctionNodeOfTheFirstKind<>(this);
    case "W":
      return new LambertWFunctionNode<>(this);
    case "j":
      return new SphericalBesselFunctionNodeOfTheFirstKind<>(this);
    case "ℭ":
    case "binom":
    case "binomial":
      return new BinomialCoefficientNode<>(this);
    case "pFq":
    case "pfq":
      return new HypergeometricFunctionNode<>(this);
    case "FoxH":
    case "foxh":
      return new FoxHFunctionNode<>(this);
    case "Beta":
    case "beta":
      return new BetaFunctionNode<>(this);
    case "Γ":
    case "Gamma":
      return new GammaFunctionNode<>(this);
    case "lnGamma":
    case "lnΓ":
    case "logGamma":
    case "logΓ":
      return new LogGammaJetNode<>(this);
    case "si":
      return new SineIntegralJetNode<>(this);
    default:

      // Issue #1018: bridge `name(arg)` to a registered context variable
      // whose runtime class is a ReifiedFunction (a concrete function-valued
      // object that carries its own indeterminate name) — e.g. a
      // ComplexPolynomial registered as `p`.
      //
      // Two cases by the argument's identity:
      //
      // 1. The argument is a variable node whose name equals the
      // ReifiedFunction's getIndependentVariableName(): the call is
      // symbolic identity — p(v) means "p whose indeterminate is v" —
      // so resolve to the polynomial itself, no evaluation. The
      // surrounding expression then operates on the polynomial as it
      // would on a bare `p`.
      //
      // 2. Otherwise the argument is a different point: build a
      // FunctionalEvaluationNode which emits evaluate(arg, ...) at
      // codegen, exactly as the postfix-`(` path does for chained
      // calls like a(k)(v).
      if (context != null)
      {
        Named existingVariable = context.variables.get(reference.name);
        if (existingVariable != null && arb.functions.ReifiedFunction.class.isAssignableFrom(existingVariable.getClass()))
        {
          Node<D, C, F> argumentNode = resolve();
          require(')');

          // The ReifiedFunction's runtime indeterminate name is whatever
          // gets injected via setIndependentVariableName(...) at evaluate
          // time — issue #1014. At parse time the binding source is the
          // enclosing reified-functional expression's placeholderVariable.
          // If the call's argument names that same placeholder, the call
          // is symbolic identity: p(v) means "p whose indeterminate is v"
          // when the enclosing arrow declared v as the placeholder, so
          // resolve to the polynomial itself, no evaluation.
          String placeholderName = placeholderVariable != null ? placeholderVariable.reference.name : null;

          if (placeholderName != null && argumentNode.isVariable() && placeholderName.equals(argumentNode.asVariable().reference.name))
          {
            // Identity in the polynomial sense: return `p` directly.
            return newVariableNode(reference);
          }

          return new FunctionalEvaluationNode<>(this,
                                                newVariableNode(reference),
                                                argumentNode);
        }
      }

      // Regular function call with parentheses
      return new FunctionNode<>(reference.name,
                                resolve(),
                                require(')'));
    }
  }

  Node<D, C, F> resolveFunctionDerivative(VariableReference<D, C, F> functionReference)
  {
    var node = require('(').resolveFunction(functionReference);
    if (node.isFunction())
    {
      var inputVariableNode = node.asFunction().getInputVariableNode();
      if (inputVariableNode != null)
      {
        var splicedVariable = inputVariableNode.spliceInto(node.expression).asVariable();
        var derivative      = node.differentiate(splicedVariable);
        return derivative;
      }

    }

    // this should differentiate with respect to the argument variable of the
    // function and then apply the argument
    // to the derivative of the function, rather than differentiating with respect
    // to the independent variable of the expression it is contained within
    return node.derivative();
  }

  Node<D, C, F> resolveFunctionSecondDerivative(VariableReference<D, C, F> reference)
  {
    var firstDerivative = resolveFunctionDerivative(reference);
    return firstDerivative.derivative();
  }

  protected Node<D, C, F> resolveIdentifier() throws CompilerException
  {
    int startPos  = position;
    var reference = resolveVariableReference();

    if (nextCharacterIs('⁻'))
    {
      if (nextCharacterIs('¹'))
      {
        if (nextCharacterIs('('))
        {
          var argument = resolve();
          require(')');
          return new InverseFunctionNode<>(reference.name,
                                           argument,
                                           this);
        }
        else
        {
          return newVariableNode(reference).pow(newLiteralConstant("-1"));
        }
      }
    }

    if (nextCharacterIs('('))
    {
      return resolveFunction(reference);
    }

    if (nextCharacterIs(COMBINING_DOT_ABOVE))
    {
      return resolveFunctionDerivative(reference);
    }

    if (nextCharacterIs(COMBINING_TWO_DOTS_ABOVE))
    {
      return resolveFunctionSecondDerivative(reference);
    }

    return resolveSymbolicLiteralConstantKeywordOrVariable(reference);
  }

  /**
   * 
   * @return the results of this{@link #resolveSquareBracketedIndex()} and
   *         this{@link #resolveSubscriptedIndex()} being applied in succession
   */
  protected Node<D, C, F> resolveIndex()
  {
    var index = resolveSquareBracketedIndex();
    return index == null ? index = resolveSubscriptedIndex() : index;
  }

  protected Node<D, C, F> resolveNumericLiteralConstant()
  {
    int startingPosition = position;
    while (isNumeric(character))
    {
      nextCharacter();
    }
    assert position > startingPosition : "didn't read any digits";

    return newLiteralConstant(getExpression().substring(startingPosition, position));
  }

  public Node<D, C, F> resolveOperand() throws CompilerException
  {
    Node<D, C, F> node = null;

    if (nextCharacterIs('['))
    {
      node = new VectorNode<>(this);
    }
    else if (nextCharacterIs('(', '⁽'))
    {
      node = resolve();
      require(')', '⁾');
    }
    else if (nextCharacterIs('∂'))
    {
      node = new DerivativeNode<>(this);
    }
    else if (nextCharacterIs('Đ'))
    {
      node = resolveCaputoFractionalDerivative();
    }
    else if (nextCharacterIs('∫'))
    {
      node = new IntegralNode<>(this);
    }
    else if (nextCharacterIs('Π', '∏'))
    {
      node = new ProductNode<>(this);
    }
    else if (nextCharacterIs('∑', 'Σ'))
    {
      node = new SumNode<>(this);
    }
    else if (isNumeric(character))
    {
      node = resolveNumericLiteralConstant();
    }
    else if (isIdentifierCharacter())
    {
      String arrowVar = parseExplicitInputVariableIfPresent();
      if (arrowVar != null)
      {
        // Issue #1014: when the codomain is a reified functional (concrete
        // class that carries its own indeterminate — ComplexPolynomial,
        // RealPolynomial, RationalFunction, ComplexRationalFunction), the
        // arrow does NOT create a separate generated class. It just names
        // the indeterminate of the returned reified-functional. Parse the
        // body on `this` so all literals/intermediates/referenced variables
        // land on the class-owning expression, and remember the arrow name
        // so generateEvaluationMethod can emit a final
        // result.setIndependentVariableName(name) call. Without this we
        // would fall into parseInputVariableAssignment, which clones this
        // Expression into a subExpr sharing the same className but with its
        // own literalConstants/intermediateVariables/referencedVariables
        // maps that declareFields() never reads — producing the
        // NoSuchFieldError of issue #1015.
        if (isReifiedFunctional())
        {
          // Register `arrowVar` as the placeholder of THIS expression so any
          // body reference to it resolves as the indeterminate (placeholder)
          // — not as a context variable picked up by first-seen-resolution.
          // generateEvaluationMethod reads the name back off the placeholder
          // to emit the final result.setIndependentVariableName(name) call.
          if (placeholderVariable == null)
          {
            setPlaceholderVariable(newVariableNode(arrowVar));
          }
          node = resolve();
        }
        else
        {
          node = parseInputVariableAssignment(arrowVar);
        }
      }
      else
      {
        node = resolveIdentifier();
      }
    }
    else if (nextCharacterIs('ꟲ'))
    {
      int  savedPos  = position;
      char savedChar = character;
      if (nextCharacterIs('D'))
      {
        node = resolveCaputoFractionalDerivative();
      }
      else
      {
        position  = savedPos;
        character = savedChar;
      }
    }

    return resolvePostfixOperators(node);
  }

  protected Node<D, C, F> resolvePostfixOperators(Node<D, C, F> node)
  {
    node = resolveFactorials(node);
    node = resolveFloor(node);
    node = resolveCeiling(node);
    node = resolveAbsoluteValue(node);
    if (nextCharacterIs('('))
    {
      FunctionalEvaluationNode.promoteDomainToScalarOfReifiedFunctional(this, node);
      Node<D, C, F> arg = resolve();
      node = buildPostfixCall(node, arg);
      while (nextCharacterIs(','))
      {
        node = buildPostfixCall(node, resolve());
      }
      require(')');
    }
    var indexNode = resolveSquareBracketedIndex();
    if (indexNode != null)
    {
      node = new IndexAccessNode<>(this,
                                   node,
                                   indexNode);
    }
    return node;
  }

  /**
   * Build a postfix function-call node, folding identity-substitution when the
   * receiver is a {@link arb.functions.ReifiedFunction} (e.g. a polynomial) and
   * the argument names this enclosing expression's placeholder variable. In that
   * case the call is symbolic identity — {@code p(v)} where {@code v} is
   * {@code p}'s indeterminate means {@code p} itself — so the receiver is
   * returned directly without wrapping in a {@link FunctionalEvaluationNode}.
   *
   * <p>
   * This complements the prefix-call identity fold at the
   * {@code context.variables.get(reference.name)} path above; that fold handles
   * bare-name calls like {@code p(v)}, while this fold handles chained /
   * sequence-element calls like {@code a(k-1)(v)}.
   */
  private Node<D, C, F> buildPostfixCall(Node<D, C, F> receiver, Node<D, C, F> arg)
  {
    Class<?> receiverType = receiver.type();
    if (receiverType != null && arb.functions.ReifiedFunction.class.isAssignableFrom(receiverType) && placeholderVariable != null && arg.isVariable()
                  && placeholderVariable.reference.name.equals(arg.asVariable().reference.name))
    {
      return receiver;
    }
    return new FunctionalEvaluationNode<>(this,
                                          receiver,
                                          arg);
  }

  protected Node<D, C, F> resolveSquareBracketedIndex()
  {
    Node<D, C, F> index = null;
    if (nextCharacterIs('['))
    {
      index = resolve();
      require(']');
    }
    return index;
  }

  protected Node<D, C, F> resolveSubscriptedIndex()
  {
    int startPos = position;

    if (nextCharacterIs(Parser.SUBSCRIPT_DIGITS_ARRAY))
    {
      while (nextCharacterIs(Parser.SUBSCRIPT_DIGITS_ARRAY));
      return newLiteralConstant(getExpression().substring(startPos, position));
    }
    else if (isIdentifierCharacter())
    {
      return resolveIdentifier();
    }
    return null;
  }

  protected Node<D, C, F> resolveSymbolicLiteralConstantKeywordOrVariable(VariableReference<D, C, F> reference)
  {
    if (reference.isLiteralConstant())
    {
      return newLiteralConstant(reference.name);
    }
    else
    {
      return reference.isElse() ? otherwise() : newVariableNode(reference);
    }
  }

  protected VariableReference<D, C, F> resolveVariableReference()
  {
    String identifier = parseName();
    var    index      = resolveIndex();
    return new VariableReference<D, C, F>(identifier,
                                          index);
  }


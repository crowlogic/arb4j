  /**
   * Emit the public, synchronized {@code evaluate(...)} wrapper that enforces
   * single-threaded, non-re-entrant invocation (#1034) and delegates the
   * actual computation to a private {@code evaluate_body(...)} helper.
   *
   * <p>The wrapper:
   * <ol>
   *   <li>checks the {@code evaluating} field; if already true, throws
   *       {@link CompilerException} naming the offence;</li>
   *   <li>sets {@code evaluating = true};</li>
   *   <li>calls {@code evaluate_body(...)} inside a try/finally — the finally
   *       resets {@code evaluating = false} on every exit (normal return or
   *       exception), so an early return inside the body cannot leave the
   *       flag stuck.</li>
   * </ol>
   *
   * The body method is emitted by
   * {@link #generateEvaluationBodyMethod(ClassVisitor)} and contains the
   * same code the previous monolithic {@code evaluate} held.
   */
  protected ClassVisitor generateEvaluationMethod(ClassVisitor classVisitor) throws CompilerException
  {
    if (rootNode == null)
    {
      define(true);
    }

    Label startLabel       = new Label();
    Label endLabel         = new Label();
    Label guardCheckLabel  = new Label();
    Label tryStart         = new Label();
    Label tryEnd           = new Label();
    Label exceptionHandler = new Label();

    var   mv               = visitEvaluationMethod(classVisitor);
    mv.visitCode();

    designateLabel(mv, startLabel);
    Compiler.annotateWithOverride(mv);

    // Re-entrancy model: the guard protects evaluate_body, which clobbers this
    // instance's scratch fields and therefore must never be re-entered. A cache
    // *read*, by contrast, depends on and mutates no instance state, so it is
    // safe to serve re-entrantly. We therefore peek the (index, order)-keyed
    // cache HERE, ahead of the guard: a re-entrant call that asks for an
    // already-computed value returns it immediately, while a re-entrant call
    // that misses (a genuine attempt to recompute mid-flight) falls through to
    // the guard and is rejected. The peek runs for every order — the cache is
    // keyed by order so an order-N read never collides with an order-M entry.
    if (shouldCache())
    {
      generateReifiedNullResultAllocation(mv);
      generateCachePeek(mv);
    }

    if (evaluateGuardEnabled)
    {
      // Same-thread re-entry guard. ACC_SYNCHRONIZED on the method handles
      // cross-thread; this boolean catches same-thread (the monitor is
      // re-entrant, the boolean is not).
      loadThisOntoStack(mv);
      mv.visitFieldInsn(Opcodes.GETFIELD, internalName(), "evaluating", "Z");
      mv.visitJumpInsn(Opcodes.IFEQ, guardCheckLabel);
      mv.visitTypeInsn(Opcodes.NEW, Type.getInternalName(CompilerException.class));
      duplicateTopOfTheStack(mv);
      mv.visitLdcInsn("re-entrant evaluate() call on same instance");
      Compiler.invokeConstructor(mv, CompilerException.class, String.class);
      mv.visitInsn(Opcodes.ATHROW);

      designateLabel(mv, guardCheckLabel);
      loadThisOntoStack(mv);
      mv.visitInsn(Opcodes.ICONST_1);
      mv.visitFieldInsn(Opcodes.PUTFIELD, internalName(), "evaluating", "Z");

      mv.visitTryCatchBlock(tryStart, tryEnd, exceptionHandler, null);
      designateLabel(mv, tryStart);

      // Delegate to evaluate_body(in, order, bits, result).
      loadThisOntoStack(mv);
      loadInputParameterChecked(mv);
      mv.visitVarInsn(Opcodes.ILOAD, 2); // order
      mv.visitVarInsn(Opcodes.ILOAD, 3); // bits
      mv.visitVarInsn(Opcodes.ALOAD, 4); // result
      mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL, internalName(), "evaluate_body", Compiler.evaluationMethodDescriptor, false);

      designateLabel(mv, tryEnd);
      loadThisOntoStack(mv);
      mv.visitInsn(Opcodes.ICONST_0);
      mv.visitFieldInsn(Opcodes.PUTFIELD, internalName(), "evaluating", "Z");
      mv.visitInsn(Opcodes.ARETURN);

      // Exception handler: reset the flag and rethrow on any exit path.
      designateLabel(mv, exceptionHandler);
      loadThisOntoStack(mv);
      mv.visitInsn(Opcodes.ICONST_0);
      mv.visitFieldInsn(Opcodes.PUTFIELD, internalName(), "evaluating", "Z");
      mv.visitInsn(Opcodes.ATHROW);
    }
    else
    {
      // No guard (ARB4J_EVALUATE_GUARD=false): call evaluate_body directly.
      loadThisOntoStack(mv);
      loadInputParameterChecked(mv);
      mv.visitVarInsn(Opcodes.ILOAD, 2); // order
      mv.visitVarInsn(Opcodes.ILOAD, 3); // bits
      mv.visitVarInsn(Opcodes.ALOAD, 4); // result
      mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL, internalName(), "evaluate_body", Compiler.evaluationMethodDescriptor, false);
      mv.visitInsn(Opcodes.ARETURN);
    }

    designateLabel(mv, endLabel);
    declareEvaluateMethodArguments(mv, startLabel, endLabel);

    mv.visitMaxs(0, 0);
    mv.visitEnd();

    generateEvaluationBodyMethod(classVisitor);
    return classVisitor;
  }

  /**
   * Emit the private {@code evaluate_body(...)} helper that holds the actual
   * evaluation logic (the same code that lived inline in
   * {@link #generateEvaluationMethod} prior to the #1034 wrapper split).
   * Generated as a private virtual method with the same descriptor as
   * {@code evaluate} so the synchronized wrapper can {@code INVOKEVIRTUAL}
   * straight into it.
   */
  protected ClassVisitor generateEvaluationBodyMethod(ClassVisitor classVisitor) throws CompilerException
  {
    nextLocalVariableSlot = 5;

    Label startLabel  = new Label();
    Label endLabel    = new Label();
    Label taylorLabel = new Label();

    var   mv          = classVisitor.visitMethod(Opcodes.ACC_PRIVATE, "evaluate_body", Compiler.evaluationMethodDescriptor, getEvaluationMethodSignature(), null);
    mv.visitCode();

    designateLabel(mv, startLabel);

    generateReifiedNullResultAllocation(mv);

    if (needsInitializer())
    {
      generateConditionalInitializater(mv);
    }

    boolean hasJets = !collectJetStates().isEmpty();

    if (hasJets)
    {
      // The stamp must be advanced BEFORE evaluateStaticSubexpressions runs:
      // jets hoisted into the static-subexpression method guard on
      // jetStamp_X != evalStamp, and both fields are zero on a fresh
      // instance, so incrementing after the static call would skip every
      // static jet on the first evaluation.
      generateEvalStampIncrement(mv);
    }

    if (hasStaticNodes)
    {
      generateStaticPrecisionCheck(mv);
    }

    // --- order > 1 dispatch to Taylor series path (non-jet expressions only) ---
    if (!hasJets)
    {
      mv.visitVarInsn(Opcodes.ILOAD, 2); // order
      mv.visitInsn(Opcodes.ICONST_1);
      mv.visitJumpInsn(Opcodes.IF_ICMPGT, taylorLabel);
    }

    boolean cache             = shouldCache();
    boolean cacheValueBacking = shouldCacheValueBacking();

    if (cache)
    {
      // The cache *peek* is emitted in evaluate() ahead of the re-entrancy guard
      // (a cache read touches no per-instance scratch state, so it is safe to
      // serve re-entrantly); evaluate_body only needs the *poke* setup.
      generateCachePokePrologue(mv);
    }
    if (cacheValueBacking)
    {
      generateValueBackingPeek(mv);
    }

    rootNode.isRootNode = true;
    if (isGeneratedFunctional())
    {
      generateFunctionalElement(mv);
    }
    else
    {
      rootNode.generate(mv, coDomainType);
    }

    // Issue #1014: for reified-functional results (ComplexPolynomial, etc.)
    // that were declared with an arrow (v➔body), tag the result with its
    // indeterminate name so subsequent toString/evaluate calls use the
    // user-declared symbol rather than the default "x". The arrow variable
    // is already recorded as this expression's placeholderVariable by
    // resolveOperand, so read the name from there — no separate field
    // needed. Stack top here is the result reference (left by
    // generateSetResultInvocation in root-node generation, which returns
    // the result after set()).
    if (isReifiedFunctional() && getPlaceholderVariable() != null)
    {
      // Stack top is the result reference, possibly typed as Object
      // (generic evaluate(Object, int, int, Object) signature). DUP it,
      // CHECKCAST to coDomainType so invokevirtual on the typed method
      // verifies, push the placeholder name, invoke the setter, pop the
      // returned (this) reference; the original result reference remains
      // for ARETURN.
      duplicateTopOfTheStack(mv);
      mv.visitTypeInsn(Opcodes.CHECKCAST, Type.getInternalName(coDomainType));
      mv.visitLdcInsn(getPlaceholderVariable().getName());
      Compiler.generateVirtualMethodInvocation(mv, coDomainType, "setIndependentVariableName", coDomainType, String.class);
      mv.visitInsn(Opcodes.POP);
    }

    if (cache)
    {
      generateCachePokeEpilogue(mv);
    }
    if (cacheValueBacking)
    {
      generateValueBackingPoke(mv);
    }

    designateLabel(mv, endLabel);
    declareEvaluateMethodArguments(mv, startLabel, endLabel);

    if (hasJets)
    {
      Compiler.generateReturnFromMethod(mv);
    }
    else
    {
      mv.visitInsn(Opcodes.ARETURN);

      // --- Taylor series evaluation for order > 1 ---
      generateTaylorSeriesPath(mv, taylorLabel);

      mv.visitMaxs(0, 0);
      mv.visitEnd();
    }
    return classVisitor;
  }

  protected Expression<Object, Object, Function<?, ?>> generateFunctionalElement(MethodVisitor mv)
  {
    var functional = newFunctionalExpression();
    functionalDependsOnIndependentVariable = false;
    functionalIndependentVariable          = null;

    if (getIndependentVariable() != null)
    {
      functionalIndependentVariable = getIndependentVariable().spliceInto(functional).asVariable();
    }

    functional.generate();

    if (functionalIndependentVariable != null)
    {
      functionalDependsOnIndependentVariable = functional.rootNode.dependsOn(functionalIndependentVariable);
    }

    constructNewObject(mv, functional.internalName());
    duplicateTopOfTheStack(mv);
    invokeDefaultConstructor(mv, functional.internalName());

    if (functionalDependsOnIndependentVariable)
    {
      copyIndependentVariableToFunctionalByValue(mv, functional, functionalIndependentVariable);
    }

    // Propagate upstream input variables (e.g. 'n' from upstream Expression's
    // scope)
    // that the functional references but that are NOT the current expression's
    // independent variable (already handled above).
    propagateUpstreamInputVariablesToFunctional(mv, functional);

    if (context != null)
    {
      propagateContext(mv, functional);
      // Also propagate the `context` field itself so the functional's own
      // initialize() — and any subsequent runtime lookups it performs via
      // {@link Context#lookupFunctionInstance} — see the same live, populated
      // Context. Without this, the freshly-allocated functional keeps its
      // field-initializer default (`new Context()`) which has no functions
      // registered, so transitive references like `m → a` resolve to null.
      // Stack on entry: [..., funcRef] → exit: [..., funcRef]
      String contextTypeDesc = Type.getDescriptor(Context.class);
      duplicateTopOfTheStack(mv); // [..., funcRef, funcRef]
      loadThisOntoStack(mv); // [..., funcRef, funcRef, this]
      mv.visitFieldInsn(GETFIELD, internalName(), "context", contextTypeDesc); // [..., funcRef, funcRef, ctx]
      mv.visitFieldInsn(PUTFIELD, functional.internalName(), "context", contextTypeDesc); // [..., funcRef]
    }

    invokeInitializationMethod(mv, functional);

    return functional.compile();
  }

  protected MethodVisitor generateFunctionInitializer(MethodVisitor mv, FunctionMapping<?, ?, ?> nestedFunction, List<String> assignments)
  {
    assert nestedFunction != null : "nestedFunction shan't be null";
    if (trace)
    {
      log.debug(String.format("generateFunctionInitializer for className=%s functionName=%s: nestedFunction=%s, assignments=%s )",
                              className,
                              functionName,
                              nestedFunction.functionName,
                              assignments));
    }

    boolean haveLiveInstance         = nestedFunction.instance != null && nestedFunction.isGenerated();

    // Pre-registered branch: a mapping created via
    // Function.parseCompileAndRegister has no `instance` yet (deferred to
    // break the chicken-and-egg cycle in mutually-recursive sequences
    // such as a ↔ S in the fractional Riccati Müntz recurrence,
    // issue #982). The Expression itself, however, is parsed and its
    // generated-class name is fixed at registration time, so we can still
    // emit the v/μ-propagation bytecode using the Expression's className
    // as a symbolic reference — ASM's GETFIELD/PUTFIELD accept name
    // strings and the JVM resolves the class lazily when the parent's
    // initialize() executes (by which point the recursive compile chain
    // has defined every class in the cluster). Without this branch the
    // generated initialize() body silently omits the
    // `if (this.S.v == null) this.S.v = this.v; else this.S.v.set(this.v)`
    // block for every pre-registered referenced function and crashes
    // with NPE on the first evaluate() that touches it.
    // Skip self-references at this codegen layer — the generated
    // initialize() body emits `this.<self> = new <self>(); inject v/μ`
    // later via generateSelfReference (Expression.java:2488). Emitting an
    // injection block here would dereference `this.<self>` before that
    // allocation runs and crash with NPE on the very first invocation.
    boolean isSelfReference          = nestedFunction.functionName != null && nestedFunction.functionName.equals(functionName);

    boolean haveRegisteredExpression =
                                     !haveLiveInstance && !isSelfReference && nestedFunction.expression != null && nestedFunction.expression.className != null;

    if (haveLiveInstance || haveRegisteredExpression)
    {
      var    nestedExpression        = nestedFunction.expression;
      // Propagate context variables based on whether the nested class
      // declares them as fields.
      //
      // {@code variablesDeclared=true} means declareVariables has already
      // run on the nested expression — its field set is frozen and
      // declaredVariables tells us exactly which fields exist. Honour that
      // set strictly. Pre-existing nested classes may have been compiled
      // when the context was smaller (e.g. m and a in OPS-on-muntz: the
      // muntz coefficient sequence was compiled while context held only
      // p, q, r, μ; OPS later adds p0, p1, but m's class still has no
      // fields for them — attempting PUTFIELD on a non-existent field
      // produces NoSuchFieldError at runtime).
      //
      // {@code variablesDeclared=false} means declareVariables has not
      // yet run — it will eventually emit a field for every entry in
      // context.variableEntries() at the time it runs. We cannot safely
      // narrow without forcing declareVariables (which itself needs a
      // ClassVisitor, unavailable here), so trust the parent's
      // hasDeclaredVariable: sibling cyclic peers in the same Context
      // declare the same field set, which has been the working invariant
      // for every mutually-recursive cluster prior to the OPS case.
      var    variableStream          = context.variableClassStream();
      var    declaredVariableStream  =
                                    nestedExpression.variablesDeclared ? variableStream.filter(variable -> nestedExpression.hasDeclaredVariable(variable.getLeft()))
                                                                       : variableStream.filter(variable -> hasDeclaredVariable(variable.getLeft()));
      String nestedClassInternalName = haveLiveInstance ? Type.getInternalName(nestedFunction.type()) : nestedFunction.expression.internalName();
      initializeReferencedFunctionVariableReferences(loadThisOntoStack(mv),
                                                     internalName(),
                                                     nestedClassInternalName,
                                                     nestedFunction.functionName,
                                                     declaredVariableStream);
    }
    else
    {
      registerReferencedFunction(nestedFunction.functionName, nestedFunction);
    }

    return mv;
  }


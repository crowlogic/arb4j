  /**
   * Calls this{@link #evaluateOptionalIndependentVariableSpecification()} before
   * calling this{@link #resolve()} and assigning the result to
   * this{@link #rootNode}
   * 
   * @param simplify TODO
   * 
   * @return this
   * @throws CompilerException
   */
  @SuppressWarnings("unchecked")
  public <D, C, F extends Function<? extends D, ? extends C>, PD, PC, PF extends Function<? extends PD, ? extends PC>, E extends Expression<D, C, F>>
         E
         parse(boolean simplify)
  {
    assert rootNode == null : "parse must only be called before anything else has been parsed but rootNode=" + rootNode;
    evaluateOptionalIndependentVariableSpecification();
    // cursor is already primed by evaluateOptionalIndependentVariableSpecification
    if (trace)
    {
      log.debug("#{}: parseRoot expression='{}'\n", System.identityHashCode(this), getExpression());
    }

    if (rootNode == null)
    {
      rootNode = resolve();
    }
    assert rootNode != null : "parse(): resolve() returned null for expression='" + getExpression() + "'";

    if (position < getExpression().length() && character != '=')
    {
      throwUnexpectedCharacterException();
    }

    if (simplify)
    {
      var simplifiedRootNode = rootNode.simplify();
      assert simplifiedRootNode != null : rootNode + "'s simplifiy method returned  null, its an instance of " + rootNode.getClass();
      rootNode = simplifiedRootNode;
    }

    rootNode.isRootNode = true;

    updateStringRepresentation();
    return (E) this;
  }


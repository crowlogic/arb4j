  /**
   * Performs common subexpression elimination (CSE) on the parsed expression
   * tree. Identifies nodes that compute equivalent subexpressions via
   * {@link Node#isEquivalentTo(Node)} and wraps them in {@link SharedNode}s so
   * that each common subexpression is computed once and reused.
   * 
   * @see <a href="https://github.com/crowlogic/arb4j/issues/518">#518</a>
   */
  @SuppressWarnings("unchecked")
  public Expression<D, C, F> optimize()
  {
    if (optimized)
    {
      return this;
    }
    if (rootNode == null)
    {
      define(true);
    }
    // Order matters: deduplicate jet siblings so that the static-hoisting
    // pass sees canonical JetState references, then hoist constant /
    // fixed-instance-data subtrees into StaticNodes, then eliminate any
    // remaining input-dependent common subexpressions.
    deduplicateJets();
    replaceConstantNodes();
    eliminateCommonSubexpressions();

    // #1027 / #1021: force every VariableNode in the rootNode subtree to
    // resolve its reference, and every upstream-input VariableNode in
    // referencedVariables to do the same, before generate() emits the
    // class. Without this, type resolution previously rode on the side
    // effect of BinaryOperationNode.generate()'s traceNodes-gated debug
    // path which called left.type()/right.type() and incidentally primed
    // each VariableNode.reference.type. With traceNodes=false the side
    // effect didn't fire, and downstream field-declaration could miss
    // upstream-input variables.
    if (rootNode != null)
    {
      rootNode.resolveVariables();
    }
    for (VariableNode<D, C, F> referenced : new java.util.ArrayList<>(referencedVariables.values()))
    {
      if (referenced.upstreamInput && referenced.reference.type == null)
      {
        referenced.resolveReference();
      }
    }

    optimized = true;
    return this;
  }

  private void optimizeAndGenerate()
  {
    optimize();
    generate();
    assert context != null : "context is null for "
                             + this
                             + " and superExpression="
                             + upstreamExpression
                             + " superExpression.context="
                             + upstreamExpression.context;
    assert !className.isEmpty() : "className is empty";
    compiledClass = loadFunctionClass(internalName(), instructions, context);
  }


package arb.expressions.nodes;

import java.util.Collections;

import org.objectweb.asm.MethodVisitor;
import org.scilab.forge.jlatexmath.LaTeXAtom;

import arb.Field;
import arb.Typesettable;
import arb.expressions.Expression;
import arb.expressions.Variables;
import arb.functions.Function;

/**
 */
public abstract class Node<D, R, F extends Function<? extends D, ? extends R>> implements
                          Typesettable
{
  public static boolean      verbose  = false;

  public Expression<D, R, F> expression;

  public boolean             isResult = false;

  public Node(Expression<D, R, F> expression, int depth)
  {
    this.expression = expression;
    this.namespace  = expression.variables;
    verbose         = expression.verbose;
  }

  final Variables<R> namespace;

  public abstract MethodVisitor generate(MethodVisitor mv);

  protected static String indent(int depth)
  {
    return String.join("", Collections.nCopies(Math.max(depth, 0), " "));
  }

  public abstract String toString(int depth);

  public int bits = 128;

  public int depth;

  /**
   * Determines whether this {@link Expression} {@link Node} is reusable. If it's
   * reusable, it means that the result {@link Field} instance generated by this
   * node can be safely reused by the parent node in the {@link Expression} tree
   * to store its own result, thus saving heap allocations. The method should be
   * overridden by every subclass of {@link Node} to provide an appropriate
   * response. For {@link LiteralConstant} and {@link Variable} nodes, they would
   * always return false (since we cannot reuse them). For {@link BinaryOperation}
   * nodes like {@link Add}, a method of determining whether they're reusable
   * based on the state of their operands.
   *
   * @return true if the node this reusable, false otherwise.
   */
  public abstract boolean isReusable();

  public abstract MethodVisitor prepareStackForReuse(MethodVisitor mv);

  /**
   * 
   * @return the {@link LaTeXAtom} string that represents this node
   */
  public abstract String typeset();

}

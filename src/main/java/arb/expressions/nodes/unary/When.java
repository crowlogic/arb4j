package arb.expressions.nodes.unary;

import static arb.expressions.Compiler.loadInput;
import static java.lang.String.format;
import static org.objectweb.asm.Opcodes.GOTO;

import java.util.TreeMap;
import java.util.stream.Collectors;

import org.objectweb.asm.Label;
import org.objectweb.asm.MethodVisitor;
import org.objectweb.asm.Opcodes;
import org.objectweb.asm.Type;

import arb.Integer;
import arb.exceptions.ExpressionCompilerException;
import arb.expressions.Compiler;
import arb.expressions.Expression;
import arb.expressions.nodes.LiteralConstant;
import arb.expressions.nodes.Node;
import arb.expressions.nodes.Variable;
import arb.functions.Function;

/**
 * The {@link When} class represents a {@link Node} in the {@link Expression}
 * that is generated by the {@link When#parse(Expression, int)} function when the
 * 'when' "function" is encountered. It's syntax is like this:
 * <code>Function&lt;Integer,Real> f = Function.express(Real.class,Integer.class,"when(n=0,1,n=1,tanh(ln(x)),else,-1)", context);</code>
 * <br>
 * <br>
 * arb4j is made available under the terms of the Business Source License™ v1.1
 * ©2024 which can be found in the root directory of this project in a file
 * named License.pdf, License.txt, or License.tm which are the pdf, text, and
 * TeXmacs formatted versions of the same document respectively.
 */
public class When<D, R, F extends Function<D, R>> extends
                 UnaryOperation<D, R, F>
{

  @Override
  public MethodVisitor generate(MethodVisitor mv, Class<?> resultType)
  {
    assert expression.domainType.equals(Integer.class) : String.format("expression.domain = %s != Integer, the only type supported presently\n",
                                                                       expression.domainType);
    try
    {
      mv.visitCode();

      labels = cases.entrySet()
                    .stream()
                    .map(entry -> new Label())
                    .collect(Collectors.toList())
                    .toArray(new Label[cases.size()]);

      cases.entrySet()
           .stream()
           .map(entry -> new Label())
           .collect(Collectors.toList())
           .toArray(new Label[cases.size()]);

      cases.values().forEach(val ->
      {
        val.isResult = isResult;
      });
      arg.isResult = isResult;

      Compiler.checkClassCast(loadInput(mv), expression.domainType);
      mv.visitMethodInsn(Opcodes.INVOKEVIRTUAL,
                         Type.getInternalName(Integer.class),
                         "getSignedValue",
                         Type.getMethodDescriptor(Type.getType(int.class)),
                         false);
      mv.visitTableSwitchInsn(0, cases.size() - 1, defaultLabel, labels);
      var branches = cases.values().stream().collect(Collectors.toList());

      for (int i = 0; i < labels.length; i++)
      {
        mv.visitLabel(labels[i]);
        branches.get(i).generate(mv, expression.rangeType);
        mv.visitJumpInsn(GOTO, endSwitch);

      }

      mv.visitLabel(defaultLabel);
      super.generate(mv, resultType);
      mv.visitLabel(endSwitch);

    }
    finally
    {
      mv.visitEnd();

    }

    return mv;
  }

  @Override
  public String toString()
  {
    return String.format("When[cases=%s,default=%s]", cases, arg);
  }

  public TreeMap<Integer, Node<D, R, F>> cases;
  private Label                          endSwitch    = new Label();
  private Label                          defaultLabel = new Label();
  private Label[]                        labels       = null;

  public When(Node<D, R, F> node, Expression<D, R, F> expression, int depth)
  {
    super(node,
          expression,
          depth);
  }

  public When(Expression<D, R, F> expression,
              TreeMap<Integer, Node<D, R, F>> cases,
              Node<D, R, F> defaultValue,
              int depth)
  {
    super(defaultValue,
          expression,
          depth);
    this.cases = cases;
  }

  @Override
  public String typeset()
  {
    return "when";
  }

  @Override
  public Class<?> type()
  {
    return expression.rangeType;
  }

  public static <D, R, F extends Function<D, R>> Node<D, R, F> parse(Expression<D, R, F> expression, int depth)
  {
    TreeMap<Integer, Node<D, R, F>> cases        = new TreeMap<>();
    Node<D, R, F>                   defaultValue = null;

    do
    {
      defaultValue = parseWhenCondition(expression, depth, cases);
    }
    while (expression.parse(depth + 1, ','));
    if (!expression.parse(depth + 1, ')'))
    {
      throw new ExpressionCompilerException("Closing parenthesis expected at position=" + expression.position
                    + " of expression=" + expression);
    }
    if (defaultValue == null)
    {
      throw new ExpressionCompilerException("default value of when function not specified with else keyword at position="
                    + expression.position + " of expression=" + expression);
    }
    return new When<D, R, F>(expression,
                             cases,
                             defaultValue,
                             depth + 1);
  }

  public static <D, R, F extends Function<D, R>>
         Node<D, R, F>
         parseWhenCondition(Expression<D, R, F> expression, int depth, TreeMap<Integer, Node<D, R, F>> cases)
  {
    Node<D, R, F> defaultValue = null;

    Node<D, R, F> node         = expression.parse(depth + 1);
    if (!(node instanceof Variable))
    {
      throw new ExpressionCompilerException("condition of when statement must be the equality of the input variable, but got "
                    + node);
    }

    Variable<D, R, F> variable = (Variable<D, R, F>) node;

    if ("else".equals(variable.reference.name))
    {
      if (!expression.parse(depth + 1, ','))
      {
        throw new ExpressionCompilerException(", expected after else condition");
      }
      defaultValue = expression.parseFirst(depth + 1);

      if (expression.ch != ')')
      {
        throw new ExpressionCompilerException(format("expected closing ) of when statement after else at position=%d expression=%s",
                                                     expression.position,
                                                     expression));
      }
    }
    else
    {
      if (!variable.reference.equals(expression.independentVariableNode.reference))
      {
        throw new ExpressionCompilerException("condition of when statement must be the equality of the input variable which is "
                      + expression.independentVariableNode + " not " + variable);
      }

      if (!expression.parse(depth + 1, '='))
      {
        throw new ExpressionCompilerException(format("= expected in condition of when function at pos=%d expression=%s but got ch=%c and lastCh=%c",
                                                     expression.position,
                                                     expression,
                                                     expression.ch,
                                                     expression.lastCh));
      }

      Node<D, R, F> condition = expression.parse(depth + 1);
      if (!(condition instanceof LiteralConstant))
      {
        throw new ExpressionCompilerException("condition of when statement must be the equality of the input variable to an "
                      + "Integer LiteralConstant type, but got " + condition);
      }
      LiteralConstant<D, R, F> constant = (LiteralConstant<D, R, F>) condition;
      if (!expression.parse(depth + 1, ','))
      {
        throw new ExpressionCompilerException(", expected after condition of when function at pos="
                      + expression.position);
      }
      Node<D, R, F> value = expression.parseFirst(depth + 1);
      cases.put(new Integer(constant.value), value);
    }
    return defaultValue;
  }

}

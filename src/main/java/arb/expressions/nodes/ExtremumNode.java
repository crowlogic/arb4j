package arb.expressions.nodes;

import static arb.expressions.Compiler.loadThisOntoStack;
import static org.objectweb.asm.Opcodes.*;

import java.util.*;
import java.util.function.Consumer;

import org.objectweb.asm.*;

import arb.Real;
import arb.expressions.Expression;
import arb.expressions.nodes.binary.BinaryOperationNode;
import arb.functions.Function;

/**
 * Implements the {@code max(a,b)} and {@code min(a,b)} built-in functions for
 * the expression compiler. Both operands are evaluated, compared via the type's
 * natural ordering ({@code Comparable}), and the selected operand is copied
 * into the node's output field.
 *
 * <p>
 * The two operands are first stored into intermediate fields so that the
 * comparison and the subsequent selection each read a stable value; the winner
 * is then copied into the output via the type's {@code set} method. The
 * generated bytecode is branchy but allocation-free beyond the two scratch
 * fields.
 *
 * @see BinaryOperationNode — the symmetric type-resolution map is reused here
 * @author Stephen Crowley ©2024-2025
 * @see arb.documentation.BusinessSourceLicenseVersionOnePointOne © terms
 */
public class ExtremumNode<D, R, F extends Function<? extends D, ? extends R>> extends
                         Node<D, R, F>
{
  public Node<D, R, F> left;

  public Node<D, R, F> right;

  public final boolean isMax;

  public ExtremumNode(Expression<D, R, F> expression, Node<D, R, F> left, Node<D, R, F> right, boolean isMax)
  {
    super(expression);
    this.left  = left;
    this.right = right;
    this.isMax = isMax;
  }

  @Override
  public Class<?> type()
  {
    Class<?> leftType  = left.type();
    Class<?> rightType = right.type();
    if (leftType.equals(rightType))
    {
      return leftType;
    }
    var innerTypeMap = BinaryOperationNode.typeMap.get(leftType);
    if (innerTypeMap != null)
    {
      Class<?> t = innerTypeMap.get(rightType);
      if (t != null)
      {
        return t;
      }
    }
    innerTypeMap = BinaryOperationNode.typeMap.get(rightType);
    if (innerTypeMap != null)
    {
      Class<?> t = innerTypeMap.get(leftType);
      if (t != null)
      {
        return t;
      }
    }
    // No explicit promotion registered: fall back to the wider of the two.
    return Real.class.isAssignableFrom(leftType) || Real.class.isAssignableFrom(rightType) ? Real.class : leftType;
  }

  @Override
  public MethodVisitor generate(MethodVisitor mv, Class<?> resultType)
  {
    Class<?> t = type();
    generatedType = t;

    String descriptor = Type.getDescriptor(t);

    // Two scratch fields hold the evaluated operands so the comparison and the
    // selection read stable references.
    String leftField  = expression.newIntermediateVariable("extL", t);
    String rightField = expression.newIntermediateVariable("extR", t);

    // Evaluate left, store into leftField
    left.generate(mv, t);
    loadThisOntoStack(mv);
    mv.visitInsn(SWAP);
    mv.visitFieldInsn(PUTFIELD, expression.internalName(), leftField, descriptor);

    // Evaluate right, store into rightField
    right.generate(mv, t);
    loadThisOntoStack(mv);
    mv.visitInsn(SWAP);
    mv.visitFieldInsn(PUTFIELD, expression.internalName(), rightField, descriptor);

    // Load both operands for comparison
    expression.loadThisAndFieldOntoStack(mv, leftField, t);
    expression.loadThisAndFieldOntoStack(mv, rightField, t);

    // left.compareTo(right) -> int
    mv.visitMethodInsn(INVOKEVIRTUAL, Type.getInternalName(t), "compareTo", "(" + descriptor + ")I", false);

    Label pickRight = new Label();
    Label end       = new Label();
    if (isMax)
    {
      // left < right -> take right
      mv.visitJumpInsn(IFLT, pickRight);
    }
    else
    {
      // left > right -> take right
      mv.visitJumpInsn(IFGT, pickRight);
    }

    // left is the extremum
    expression.loadThisAndFieldOntoStack(mv, leftField, t);
    mv.visitJumpInsn(GOTO, end);

    // right is the extremum
    mv.visitLabel(pickRight);
    expression.loadThisAndFieldOntoStack(mv, rightField, t);

    mv.visitLabel(end);
    // selected value sits on the stack

    // Copy the selected value into the output field
    loadOutputVariableOntoStack(mv, t);
    mv.visitInsn(SWAP);
    mv.visitMethodInsn(INVOKEVIRTUAL, Type.getInternalName(t), "set", "(" + descriptor + ")" + descriptor, false);

    return mv;
  }

  @Override
  public String toString()
  {
    return String.format("%s(%s, %s)", isMax ? "max" : "min", left, right);
  }

  @Override
  public void accept(Consumer<Node<D, R, F>> visitor)
  {
    if (left != null)
    {
      left.accept(visitor);
    }
    if (right != null)
    {
      right.accept(visitor);
    }
    visitor.accept(this);
  }

  @Override
  public boolean dependsOn(VariableNode<D, R, F> variable)
  {
    return (left != null && left.dependsOn(variable)) || (right != null && right.dependsOn(variable));
  }

  @Override
  public Node<D, R, F> differentiate(VariableNode<D, R, F> variable)
  {
    throw new UnsupportedOperationException("differentiation of max/min is not supported");
  }

  @Override
  public Node<D, R, F> integral(VariableNode<D, R, F> variable)
  {
    throw new UnsupportedOperationException("integration of max/min is not supported");
  }

  @Override
  public List<? extends Node<D, R, F>> getBranches()
  {
    var branches = new ArrayList<Node<D, R, F>>();
    if (left != null)
    {
      branches.add(left);
    }
    if (right != null)
    {
      branches.add(right);
    }
    return branches;
  }

  @Override
  public <E, S, G extends Function<? extends E, ? extends S>> Node<E, S, G> spliceInto(Expression<E, S, G> newExpression)
  {
    Node<E, S, G> splicedLeft  = left.spliceInto(newExpression);
    Node<E, S, G> splicedRight = right.spliceInto(newExpression);
    return new ExtremumNode<>(newExpression,
                              splicedLeft,
                              splicedRight,
                              isMax);
  }

  @Override
  public <E, S, G extends Function<? extends E, ? extends S>> Node<D, R, F> substitute(String variable, Node<E, S, G> replacement)
  {
    left  = left.substitute(variable, replacement);
    right = right.substitute(variable, replacement);
    return this;
  }

  @Override
  public char symbol()
  {
    return isMax ? '∨' : '∧';
  }

  @Override
  public String typeset()
  {
    return String.format("\\%s(%s, %s)", isMax ? "max" : "min", left.typeset(), right.typeset());
  }

  @Override
  public boolean isAtomic()
  {
    return false;
  }
}

/**
 * General relativity, also known as the general theory of relativity and
 * Einstein's theory of gravity, is the geometric theory of gravitation
 * published by Albert Einstein in 1915 and is the current description of
 * gravitation in modern physics. General relativity generalizes special
 * relativity and refines Newton's law of universal gravitation, providing a
 * unified description of gravity as a geometric property of space and time or
 * four-dimensional spacetime. In particular, the curvature of spacetime is
 * directly related to the energy and momentum of whatever matter and radiation
 * are present. The relation is specified by the Einstein field equations, a
 * system of second order partial differential equations.
 * 
 * Newton's law of universal gravitation, which describes classical gravity, can
 * be seen as a prediction of general relativity for the almost flat spacetime
 * geometry around stationary mass distributions. Some predictions of general
 * relativity, however, are beyond Newton's law of universal gravitation in
 * classical physics. These predictions concern the passage of time, the
 * geometry of space, the motion of bodies in free fall, and the propagation of
 * light, and include gravitational time dilation, gravitational lensing, the
 * gravitational redshift of light, the Shapiro time delay and
 * singularities/black holes. So far, all tests of general relativity have been
 * shown to be in agreement with the theory. The time dependent solutions of
 * general relativity enable us to talk about the history of the universe and
 * have provided the modern framework for cosmology, thus leading to the
 * discovery of the Big Bang and cosmic microwave background radiation. Despite
 * the introduction of a number of alternative theories, general relativity
 * continues to be the simplest theory consistent with experimental data.
 * 
 * Reconciliation of general relativity with the laws of quantum physics remains
 * a problem, however, as there is a lack of a self-consistent theory of quantum
 * gravity. It is not yet known how gravity can be unified with the three
 * non-gravitational forces: strong, weak and electromagnetic.
 * 
 * @see <a href="https://en.wikipedia.org/wiki/General_relativity">Wikipedia</a>
 */

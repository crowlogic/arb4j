/**
 * A part of algebraic topology which realizes a connection between topological
 * and algebraic concepts. By associating to each space a certain sequence of
 * groups, and to each continuous mapping of spaces, homomorphisms of the
 * respective groups, homology theory uses the properties of groups and their
 * homomorphisms to clarify the properties of spaces and mappings.
 * 
 * Such properties include, for example, the connections between various
 * dimensionalities, the study of which is based on the concept of excision,
 * unlike the other part of algebraic topology — the theory of homotopy, in
 * which deformations are used for the same purpose.
 * 
 * Homology theory was introduced towards the end of the 19th century by H.
 * Poincaré (cf. Homology of a polyhedron), but the axiomatic construction
 * (including the precise limits of this concept, which had been indefinite for
 * a long time) was imparted to it only by S. Eilenberg and N. Steenrod [3] (cf.
 * Algebraic topology; {@link HomologyGroup}; Steenrod–Eilenberg axioms).
 * 
 * @see <a href=
 *      "https://encyclopediaofmath.org/wiki/Homology_theory">HomologyTheory@TheEncyclopediaOfMath</a>
 */
